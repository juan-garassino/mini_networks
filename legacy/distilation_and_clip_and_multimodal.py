@torch.no_grad()
def text_to_image(model, text, stoi, max_len=16, n=8, guide_w=2.0,
                  use_ddim=False, ddim_steps=50, eta=0.0):
    ids = [stoi.get(t, 0) for t in text.lower().split()]
    ids = ids[:max_len] + [stoi["<pad>"]] * (max_len - len(ids))
    enc_tok = torch.tensor(ids, dtype=torch.long, device=DEVICE).unsqueeze(0)
    txt_emb = model.txt_enc(enc_tok)

    if model.use_latent:
        latent_size = (model.vae.latent_channels, 8, 8)
        if use_ddim:
            latents = model.diff.sample_ddim(n=n, size=latent_size, c_vec=txt_emb, guide_w=guide_w, eta=eta, ddim_steps=ddim_steps)
        else:
            latents = model.diff.sample(n=n, size=latent_size, c_vec=txt_emb, guide_w=guide_w)
        imgs = model.vae.decode(latents)
        show_images(imgs, title=f'Text→Image ({ "DDIM" if use_ddim else "DDPM"} latent): "{text}"', nrow=min(8,n), value_range=(-1,1))
        return imgs
    else:
        imgs = model.diff.sample(n=n, size=(1,28,28), c_vec=txt_emb, guide_w=guide_w)
        show_images(imgs, title=f'Text→Image (DDPM pixel): "{text}"', nrow=min(8,n), value_range=(-1,1))
        return imgs

@torch.no_grad()
def image_to_text(model, imgs, itos):
    if imgs.ndim == 3: imgs = imgs.unsqueeze(0)
    imgs = imgs.to(DEVICE)
    emb = model.img_enc(imgs)
    seq = model.cap_dec.greedy(emb, max_len=model.cap_dec.max_len)
    out = []
    for row in seq:
        words = []
        for tid in row.tolist():
            if tid in (model.cap_dec.bos_id, model.cap_dec.eos_id, model.cap_dec.pad_id): continue
            words.append(itos.get(tid, ""))
        out.append(" ".join([w for w in words if w]))
    return out

