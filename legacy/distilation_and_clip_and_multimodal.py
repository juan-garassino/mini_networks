FASHION_CLASS_TEXTS = ["t-shirt","trouser","pullover","dress","coat","sandal","shirt","sneaker","bag","ankle boot"]
MNIST_CLASS_TEXTS = [f"digit {i}" for i in range(10)]

@torch.no_grad()
def text_to_class_id(query, txt_enc, dataset="fashion"):
    labels = FASHION_CLASS_TEXTS if dataset.lower()=="fashion" else MNIST_CLASS_TEXTS
    txt_enc.eval(); q_emb = txt_enc([query]).to(DEVICE); class_embs = txt_enc(labels).to(DEVICE)
    sims = (q_emb @ class_embs.T).softmax(dim=-1); cid = sims.argmax(dim=-1).item()
    return cid, labels[cid], sims.squeeze().cpu().numpy()

@torch.no_grad()
def text_to_image(query, txt_enc, ldm, vae, dataset="fashion", n_samples=6, guide_weight=2.0,
                  n_classes=10, use_ddim=False, ddim_steps=50, eta=0.0):
    cid, cname, _ = text_to_class_id(query, txt_enc, dataset=dataset)
    print(Fore.YELLOW + f'Text→class: "{query}" → {cname} (id={cid})' + Style.RESET_ALL)
    if use_ddim:
        latents, _ = ldm.sample_ddim(n_samples=n_samples, size=(vae.latent_channels, 8, 8), device=DEVICE,
                                     guide_weight=guide_weight, class_id=cid, n_classes=n_classes,
                                     eta=eta, steps=ddim_steps)
    else:
        latents, _ = ldm.sample(n_samples=n_samples, size=(vae.latent_channels, 8, 8), device=DEVICE,
                                guide_weight=guide_weight, class_id=cid, n_classes=n_classes)
    imgs = vae.decode(latents)
    show_images(imgs, title=f'Text→Image: "{query}" → {cname}', nrow=min(6, n_samples))
    return imgs

