class ResidualConvBlock(nn.Module):
    def __init__(self, in_channels, out_channels, is_res=False):
        super().__init__()
        self.same_channels = in_channels == out_channels
        self.is_res = is_res
        self.conv1 = nn.Sequential(nn.Conv2d(in_channels, out_channels, 3, 1, 1), nn.BatchNorm2d(out_channels), nn.GELU())
        self.conv2 = nn.Sequential(nn.Conv2d(out_channels, out_channels, 3, 1, 1), nn.BatchNorm2d(out_channels), nn.GELU())
    def forward(self, x):
        if self.is_res:
            x1 = self.conv1(x); x2 = self.conv2(x1)
            out = (x + x2) if self.same_channels else (x1 + x2)
            return out / math.sqrt(2)
        else: return self.conv2(self.conv1(x))

class UnetDown(nn.Module):
    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.conv = ResidualConvBlock(in_channels, out_channels)
        self.pool = nn.MaxPool2d(2)
    def forward(self, x): h = self.conv(x); return self.pool(h), h

class UnetUp(nn.Module):
    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.up = nn.ConvTranspose2d(in_channels - out_channels, out_channels, 2, 2)
        self.res1 = ResidualConvBlock(in_channels, out_channels)
        self.res2 = ResidualConvBlock(out_channels, out_channels)
    def forward(self, x, skip):
        x = self.up(x); x = torch.cat([x, skip], dim=1)
        return self.res2(self.res1(x))

class EmbedFC(nn.Module):
    def __init__(self, input_dim, emb_dim):
        super().__init__()
        self.net = nn.Sequential(nn.Linear(input_dim, emb_dim), nn.GELU(), nn.Linear(emb_dim, emb_dim))
    def forward(self, x): return self.net(x.view(-1, x.size(-1)))

class ContextUnet(nn.Module):
    def __init__(self, in_channels=4, n_feat=128, n_classes=10):
        super().__init__()
        self.in_channels, self.n_feat, self.n_classes = in_channels, n_feat, n_classes
        self.init_conv = ResidualConvBlock(in_channels, n_feat, is_res=True)
        self.down1 = UnetDown(n_feat, n_feat)
        self.down2 = UnetDown(n_feat, 2 * n_feat)
        self.to_vec = nn.Sequential(nn.AdaptiveAvgPool2d(1), nn.GELU())
        self.timeembed1, self.timeembed2 = EmbedFC(1, 2 * n_feat), EmbedFC(1, 1 * n_feat)
        self.contextembed1, self.contextembed2 = EmbedFC(n_classes, 2 * n_feat), EmbedFC(n_classes, 1 * n_feat)
        self.up0 = nn.Sequential(nn.ConvTranspose2d(2 * n_feat, 2 * n_feat, 2, 2), nn.GroupNorm(8, 2 * n_feat), nn.ReLU())
        self.up1, self.up2 = UnetUp(3 * n_feat, n_feat), UnetUp(2 * n_feat, n_feat)
        self.out = nn.Sequential(nn.Conv2d(2 * n_feat, n_feat, 3, 1, 1), nn.GroupNorm(8, n_feat), nn.ReLU(),
                                 nn.Conv2d(n_feat, in_channels, 3, 1, 1))
    def forward(self, x, c, t, context_mask):
        x0 = self.init_conv(x)
        d1, d1_skip = self.down1(x0); d2, d2_skip = self.down2(d1)
        hidden = self.to_vec(d2)
        c_onehot = F.one_hot(c, num_classes=self.n_classes).float()
        mask = context_mask.float().unsqueeze(1); c_onehot = c_onehot * (1.0 - mask)
        t = t.view(-1, 1)
        cemb1, temb1 = self.contextembed1(c_onehot).view(-1, self.n_feat*2, 1, 1), self.timeembed1(t).view(-1, self.n_feat*2, 1, 1)
        cemb2, temb2 = self.contextembed2(c_onehot).view(-1, self.n_feat, 1, 1), self.timeembed2(t).view(-1, self.n_feat, 1, 1)
        u0 = self.up0(hidden); u1 = self.up1(cemb1 * u0 + temb1, d2_skip); u2 = self.up2(cemb2 * u1 + temb2, d1_skip)
        return self.out(torch.cat([u2, x0], dim=1))

def ddpm_schedules(beta1, beta2, T):
    beta_t = (beta2 - beta1) * torch.arange(0, T + 1, dtype=torch.float32) / T + beta1
    sqrt_beta_t = torch.sqrt(beta_t); alpha_t = 1.0 - beta_t
    log_alpha_t = torch.log(alpha_t); alphabar_t = torch.cumsum(log_alpha_t, dim=0).exp()
    sqrtab = torch.sqrt(alphabar_t); oneover_sqrta = 1.0 / torch.sqrt(alpha_t); sqrtmab = torch.sqrt(1.0 - alphabar_t)
    mab_over_sqrtmab = (1.0 - alpha_t) / torch.clamp(sqrtmab, min=1e-12)
    return {"alpha_t": alpha_t, "oneover_sqrta": oneover_sqrta, "sqrt_beta_t": sqrt_beta_t,
            "alphabar_t": alphabar_t, "sqrtab": sqrtab, "sqrtmab": sqrtmab, "mab_over_sqrtmab": mab_over_sqrtmab}

class LDM(nn.Module):
    def __init__(self, nn_model, betas, n_diffusion_steps, device, drop_prob=0.1, predict_score=False):
        super().__init__()
        self.nn_model = nn_model.to(device); self.device = device
        self.n_diffusion_steps, self.drop_prob, self.predict_score = int(n_diffusion_steps), float(drop_prob), predict_score
        self.loss_mse = nn.MSELoss()
        sched = ddpm_schedules(betas[0], betas[1], self.n_diffusion_steps)
        for k, v in sched.items(): self.register_buffer(k, v)
    def forward(self, z, context):
        B = z.size(0); t_int = torch.randint(1, self.n_diffusion_steps, (B,), device=self.device)
        noise = torch.randn_like(z)
        z_t = self.sqrtab[t_int, None, None, None] * z + self.sqrtmab[t_int, None, None, None] * noise
        context_mask = torch.bernoulli(torch.full_like(context, self.drop_prob, dtype=torch.float32, device=self.device)).long()
        t_scaled = (t_int / self.n_diffusion_steps).float().view(-1, 1)
        pred = self.nn_model(z_t, context, t_scaled, context_mask)
        target = - noise / self.sqrtmab[t_int, None, None, None] if self.predict_score else noise
        return self.loss_mse(pred, target)
    @torch.no_grad()
    def sample(self, n_samples, size, device=None, guide_weight=0.0, class_id=None, n_classes=10):
        device = device or self.device; x = torch.randn(n_samples, *size, device=device)
        labels = (torch.arange(0, n_classes, device=device).repeat(int(math.ceil(n_samples / n_classes)))[:n_samples]
                  if class_id is None else torch.full((n_samples,), int(class_id), device=device, dtype=torch.long))
        for i in range(self.n_diffusion_steps, 0, -1):
            t_scaled = torch.full((n_samples, 1), i / self.n_diffusion_steps, device=device)
            x_in, labels_in = x.repeat(2, 1, 1, 1), labels.repeat(2)
            context_mask = torch.zeros_like(labels_in, dtype=torch.long, device=device); context_mask[n_samples:] = 1
            pred = self.nn_model(x_in, labels_in, t_scaled.repeat(2, 1), context_mask)
            pred1, pred2 = pred[:n_samples], pred[n_samples:]; pred = (1 + guide_weight) * pred1 - guide_weight * pred2
            eps = - self.sqrtmab[i] * pred if self.predict_score else pred
            z = torch.randn_like(x) if i > 1 else 0.0
            x = self.oneover_sqrta[i] * (x - eps * self.mab_over_sqrtmab[i]) + self.sqrt_beta_t[i] * z
        return x, None
    @torch.no_grad()
    def sample_ddim(self, n_samples, size, device=None, guide_weight=0.0, class_id=None, n_classes=10, eta=0.0, steps=50):
        device = device or self.device; steps = max(1, min(steps, self.n_diffusion_steps))
        step_indices = torch.round(torch.linspace(0, self.n_diffusion_steps, steps + 1, device=device)).long()
        t = self.n_diffusion_steps - step_indices + 1
        x = torch.randn(n_samples, *size, device=device)
        labels = (torch.arange(0, n_classes, device=device).repeat(int(math.ceil(n_samples / n_classes)))[:n_samples]
                  if class_id is None else torch.full((n_samples,), int(class_id), device=device, dtype=torch.long))
        for i in range(steps):
            t_cur, t_next = t[i], (t[i + 1] if i + 1 < steps else 0)
            alpha_cur, alpha_next = self.alphabar_t[t_cur], (self.alphabar_t[t_next] if t_next > 0 else 1.0)
            sigma = eta * torch.sqrt(((1 - alpha_next) / (1 - alpha_cur)) * (1 - alpha_cur / alpha_next)).clamp(min=0)
            t_scaled = (t_cur / self.n_diffusion_steps).float().view(-1, 1)
            x_in, labels_in = x.repeat(2, 1, 1, 1), labels.repeat(2)
            context_mask = torch.zeros_like(labels_in, dtype=torch.long, device=device); context_mask[n_samples:] = 1
            pred = self.nn_model(x_in, labels_in, t_scaled.repeat(2, 1), context_mask)
            pred1, pred2 = pred[:n_samples], pred[n_samples:]; pred = (1 + guide_weight) * pred1 - guide_weight * pred2
            eps = - self.sqrtmab[t_cur] * pred if self.predict_score else pred
            x0_pred = (x - torch.sqrt(1 - alpha_cur) * eps) / torch.sqrt(alpha_cur)
            x_dir = torch.sqrt(1 - alpha_next - sigma**2) * eps
            x = torch.sqrt(alpha_next) * x0_pred + x_dir + sigma * torch.randn_like(x)
        return x, None

