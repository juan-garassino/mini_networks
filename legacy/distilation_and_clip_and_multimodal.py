FASHION_CLASS_TEXTS = ["t-shirt","trouser","pullover","dress","coat","sandal","shirt","sneaker","bag","ankle boot"]
MNIST_CLASS_TEXTS = [f"digit {i}" for i in range(10)]

@torch.no_grad()
def text_to_class_id(query, txt_enc, dataset="fashion"):
    if dataset.lower() == "fashion":
        labels = FASHION_CLASS_TEXTS
    else:
        labels = MNIST_CLASS_TEXTS
    txt_enc.eval()
    q_emb = txt_enc([query]).to(DEVICE)
    class_embs = txt_enc(labels).to(DEVICE)
    sims = (q_emb @ class_embs.T).softmax(dim=-1)
    cid = sims.argmax(dim=-1).item()
    return cid, labels[cid], sims.squeeze().cpu().numpy()

@torch.no_grad()
def text_to_image(query, txt_enc, ddpm, dataset="fashion", n_samples=6, guide_weight=2.0, n_classes=10):
    cid, cname, _ = text_to_class_id(query, txt_enc, dataset=dataset)
    print(Fore.YELLOW + f'Text→class: "{query}" → {cname} (id={cid})' + Style.RESET_ALL)
    imgs, _ = ddpm.sample(n_samples=n_samples, size=(1,28,28), device=DEVICE, guide_weight=guide_weight, class_id=cid, n_classes=n_classes)
    show_images(imgs, title=f'Text→Image: "{query}" → {cname}', nrow=min(6, n_samples))
    return imgs

