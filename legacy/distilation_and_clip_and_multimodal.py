@torch.no_grad()
def dual_image_oscillation(ldm, vae, size=(4,8,8), class_a=5, class_b=7, rotations=3,
                           guide_weight=2.0, n_diffusion_steps=None, flip_every=50,
                           dataset_hint="fashion", n_classes=10):
    model, T = ldm.nn_model, (n_diffusion_steps or ldm.n_diffusion_steps)
    x = torch.randn(1, *size, device=DEVICE); current_class = int(class_a); frames = []
    for i in range(T, 0, -1):
        t_scaled = torch.full((1,1), i / T, device=DEVICE)
        x_in = x.repeat(2,1,1,1); labels_in = torch.tensor([current_class, current_class], device=DEVICE, dtype=torch.long)
        context_mask = torch.tensor([0,1], device=DEVICE, dtype=torch.long)
        eps = model(x_in, labels_in, t_scaled.repeat(2,1), context_mask)
        if ldm.predict_score: eps = - ldm.sqrtmab[i] * eps
        eps1, eps2 = eps[:1], eps[1:]; eps = (1 + guide_weight) * eps1 - guide_weight * eps2
        z = torch.randn_like(x) if i > 1 else 0.0
        x = ldm.oneover_sqrta[i] * (x - eps * ldm.mab_over_sqrtmab[i]) + ldm.sqrt_beta_t[i] * z
        if i % max(1, (T // 16)) == 0 or i <= 8: frames.append(vae.decode(x).clone().cpu())
        if i % flip_every == 0 and i != T:
            x = torch.rot90(x, k=2, dims=(2,3)); current_class = class_b if current_class == class_a else class_a
    if len(frames) > 0: show_images(torch.cat(frames, dim=0), title=f"Dual Oscillation ({dataset_hint.upper()} {class_a}↔{class_b})", nrow=8)
    final_img = vae.decode(x); show_images(final_img, title=f"Final ({dataset_hint.upper()} {class_a}↔{class_b})", nrow=1)
    return final_img

