class SmallCNN(nn.Module):
    def __init__(self, num_classes=20, rep_dim=128):
        super().__init__()
        self.conv = nn.Sequential(
            nn.Conv2d(3, 32, 3, padding=1), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(32, 64, 3, padding=1), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(64, 128, 3, padding=1), nn.ReLU(), nn.AdaptiveAvgPool2d(1)
        )
        self.rep = nn.Linear(128, rep_dim)
        self.cls = nn.Linear(rep_dim, num_classes)

    def forward(self, x):
        h = self.conv(x).squeeze(-1).squeeze(-1)  # (N,128)
        rep = F.relu(self.rep(h))                 # (N,rep_dim)
        logits = self.cls(rep)                    # (N,C)
        return logits, rep

class TinyViT(nn.Module):
    def __init__(self, img_size=224, patch=16, d_model=128, n_heads=2, n_layers=2, mlp_ratio=2, num_classes=20):
        super().__init__()
        assert img_size % patch == 0
        self.patch = patch
        self.patch_embed = nn.Conv2d(3, d_model, kernel_size=patch, stride=patch, padding=0)
        self.cls_token = nn.Parameter(torch.zeros(1, 1, d_model))
        # initialize with expected (1 + 14*14) but make it dynamic if mismatch later
        num_patches = (img_size // patch) * (img_size // patch)
        self.pos_embed = nn.Parameter(torch.randn(1, 1 + num_patches, d_model) * 0.02)

        enc_layer = nn.TransformerEncoderLayer(
            d_model=d_model, nhead=n_heads,
            dim_feedforward=int(d_model * mlp_ratio),
            activation="gelu", batch_first=True, norm_first=True
        )
        self.encoder = nn.TransformerEncoder(enc_layer, num_layers=n_layers)
        self.norm = nn.LayerNorm(d_model)
        self.fc = nn.Linear(d_model, num_classes)
        nn.init.trunc_normal_(self.cls_token, std=0.02)

    def forward(self, x):
        # Patchify (robust to any slight size drift)
        x = self.patch_embed(x)            # (N,d,H',W')
        x = x.flatten(2).transpose(1,2)    # (N,P,d)
        N, P, D = x.shape
        cls = self.cls_token.expand(N, 1, D)

        # Ensure pos_embed length matches 1 + P
        need_len = 1 + P
        if self.pos_embed.size(1) != need_len:
            # re-init position embedding to match current sequence length
            self.pos_embed = nn.Parameter(torch.randn(1, need_len, D, device=x.device) * 0.02)

        x = torch.cat([cls, x], dim=1) + self.pos_embed[:, : 1 + P]
        x = self.encoder(x)
        cls_out = self.norm(x[:,0])
        logits = self.fc(cls_out)
        return logits, cls_out

class FeatureProjector(nn.Module):
    def __init__(self, in_dim, out_dim):
        super().__init__()
        self.proj = nn.Linear(in_dim, out_dim)
    def forward(self, x): return self.proj(x)

