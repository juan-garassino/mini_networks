class ResidualConvBlock(nn.Module):
    def __init__(self, in_channels: int, out_channels: int, is_res: bool = False):
        super().__init__()
        self.same_channels = in_channels == out_channels
        self.is_res = is_res
        self.conv1 = nn.Sequential(
            nn.Conv2d(in_channels, out_channels, 3, 1, 1),
            nn.BatchNorm2d(out_channels),
            nn.GELU(),
        )
        self.conv2 = nn.Sequential(
            nn.Conv2d(out_channels, out_channels, 3, 1, 1),
            nn.BatchNorm2d(out_channels),
            nn.GELU(),
        )
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        if self.is_res:
            x1 = self.conv1(x); x2 = self.conv2(x1)
            out = (x + x2) if self.same_channels else (x1 + x2)
            return out / 1.41421356237
        else:
            return self.conv2(self.conv1(x))

class UnetDown(nn.Module):
    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.model = nn.Sequential(ResidualConvBlock(in_channels, out_channels), nn.MaxPool2d(2))
    def forward(self, x): return self.model(x)

class UnetUp(nn.Module):
    """
    Upsample + concat(skip) + two residual convs.
    `in_channels_concat` is the channel count AFTER concatenation (x_up + skip).
    """
    def __init__(self, in_channels_concat, out_channels):
        super().__init__()
        self.up = nn.Upsample(scale_factor=2, mode='nearest')
        self.res1 = ResidualConvBlock(in_channels_concat, out_channels)
        self.res2 = ResidualConvBlock(out_channels, out_channels)

    def forward(self, x, skip):
        # x: (B, Cx, H, W)  -> upsample to match skip spatial size
        x = self.up(x)
        if x.shape[-2:] != skip.shape[-2:]:
            # make absolutely sure they match (defensive)
            skip = F.interpolate(skip, size=x.shape[-2:], mode='nearest')
        x = torch.cat([x, skip], dim=1)
        x = self.res1(x)
        x = self.res2(x)
        return x

class EmbedFC(nn.Module):
    def __init__(self, input_dim, emb_dim):
        super().__init__()
        self.input_dim = input_dim
        self.net = nn.Sequential(nn.Linear(input_dim, emb_dim), nn.GELU(), nn.Linear(emb_dim, emb_dim))
    def forward(self, x):
        return self.net(x.view(-1, self.input_dim))

class ContextUnet(nn.Module):
    def __init__(self, in_channels=1, n_feat=128, n_classes=10):
        super().__init__()
        self.in_channels = in_channels
        self.n_feat = n_feat
        self.n_classes = n_classes

        # Encoder
        self.init_conv = ResidualConvBlock(in_channels, n_feat, is_res=True)   # -> (n_feat, 28, 28)
        self.down1 = UnetDown(n_feat, n_feat)                                  # -> (n_feat, 14, 14)
        self.down2 = UnetDown(n_feat, 2 * n_feat)                              # -> (2n_feat, 7, 7)
        self.to_vec = nn.Sequential(nn.AvgPool2d(7), nn.GELU())                # -> (2n_feat, 1, 1)

        # Time & context embeddings
        self.timeembed1    = EmbedFC(1, 2 * n_feat)
        self.timeembed2    = EmbedFC(1, 1 * n_feat)
        self.contextembed1 = EmbedFC(n_classes, 2 * n_feat)
        self.contextembed2 = EmbedFC(n_classes, 1 * n_feat)

        # Bottleneck up (1→7)
        self.up0 = nn.Sequential(
            nn.ConvTranspose2d(2 * n_feat, 2 * n_feat, 7, 7),
            nn.GroupNorm(8, 2 * n_feat), nn.ReLU(),
        )

        # Decoder:
        # u1: up 7→14, concat with d1 (n_feat)  => total 2n_feat + n_feat = 3n_feat
        self.up1 = UnetUp(3 * n_feat, n_feat)

        # u2: up 14→28, concat with x0 (n_feat) => total n_feat + n_feat = 2n_feat
        self.up2 = UnetUp(2 * n_feat, n_feat)

        # Output head (no extra concat; x0 already used as skip above)
        self.out = nn.Sequential(
            nn.Conv2d(n_feat, n_feat, 3, 1, 1),
            nn.GroupNorm(8, n_feat), nn.ReLU(),
            nn.Conv2d(n_feat, in_channels, 3, 1, 1),
        )

    def forward(self, x, c, t, context_mask):
        # Encoder
        x0 = self.init_conv(x)    # (n_feat, 28, 28)
        d1 = self.down1(x0)       # (n_feat, 14, 14)
        d2 = self.down2(d1)       # (2n_feat, 7, 7)
        hidden = self.to_vec(d2)  # (2n_feat, 1, 1)

        # Context + time
        c_onehot = F.one_hot(c, num_classes=self.n_classes).float()
        mask = context_mask.float().unsqueeze(1)         # (B,1)
        c_onehot = c_onehot * (1.0 - mask)               # drop condition for masked examples
        t = t.view(-1, 1)

        cemb1 = self.contextembed1(c_onehot).view(-1, self.n_feat*2, 1, 1)
        temb1 = self.timeembed1(t).view(-1, self.n_feat*2, 1, 1)
        cemb2 = self.contextembed2(c_onehot).view(-1, self.n_feat, 1, 1)
        temb2 = self.timeembed2(t).view(-1, self.n_feat, 1, 1)

        # Decoder
        u0 = self.up0(hidden)                      # (2n_feat, 7, 7)
        u1 = self.up1(cemb1 * u0 + temb1, d1)     # (n_feat, 14, 14)  skip = d1
        u2 = self.up2(cemb2 * u1 + temb2, x0)     # (n_feat, 28, 28)  skip = x0

        return self.out(u2)

def ddpm_schedules(beta1: float, beta2: float, T: int):
    assert 0.0 < beta1 < beta2 < 1.0
    beta_t = (beta2 - beta1) * torch.arange(0, T + 1, dtype=torch.float32) / T + beta1
    sqrt_beta_t = torch.sqrt(beta_t)
    alpha_t = 1.0 - beta_t
    log_alpha_t = torch.log(alpha_t)
    alphabar_t = torch.cumsum(log_alpha_t, dim=0).exp()
    sqrtab = torch.sqrt(alphabar_t)
    oneover_sqrta = 1.0 / torch.sqrt(alpha_t)
    sqrtmab = torch.sqrt(1.0 - alphabar_t)
    mab_over_sqrtmab = (1.0 - alpha_t) / torch.clamp(sqrtmab, min=1e-12)
    return {
        "alpha_t": alpha_t, "oneover_sqrta": oneover_sqrta, "sqrt_beta_t": sqrt_beta_t,
        "alphabar_t": alphabar_t, "sqrtab": sqrtab, "sqrtmab": sqrtmab, "mab_over_sqrtmab": mab_over_sqrtmab
    }

class DDPM(nn.Module):
    def __init__(self, nn_model, betas, n_diffusion_steps, device, drop_prob=0.1):
        super().__init__()
        self.nn_model = nn_model.to(device)
        self.device = device
        self.n_diffusion_steps = int(n_diffusion_steps)
        self.drop_prob = float(drop_prob)
        self.loss_mse = nn.MSELoss()
        sched = ddpm_schedules(betas[0], betas[1], self.n_diffusion_steps)
        for k, v in sched.items():
            self.register_buffer(k, v)
    def forward(self, x, context):
        B = x.size(0)
        t_int = torch.randint(1, self.n_diffusion_steps, (B,), device=self.device)
        noise = torch.randn_like(x)
        x_t = self.sqrtab[t_int, None, None, None] * x + self.sqrtmab[t_int, None, None, None] * noise
        context_mask = torch.bernoulli(torch.full_like(context, self.drop_prob, dtype=torch.float32, device=self.device)).long()
        t_scaled = (t_int / self.n_diffusion_steps).float().view(-1, 1)
        pred = self.nn_model(x_t, context, t_scaled, context_mask)
        return self.loss_mse(pred, noise)
    @torch.no_grad()
    def sample(self, n_samples, size, device=None, guide_weight=0.0, class_id=None, n_classes=10):
        device = device or self.device
        x = torch.randn(n_samples, *size, device=device)
        if class_id is None:
            labels = torch.arange(0, n_classes, device=device)
            labels = labels.repeat(int(math.ceil(n_samples / n_classes)))[:n_samples]
        else:
            labels = torch.full((n_samples,), int(class_id), device=device, dtype=torch.long)
        for i in range(self.n_diffusion_steps, 0, -1):
            t_scaled = torch.full((n_samples, 1), i / self.n_diffusion_steps, device=device)
            x_in = x.repeat(2, 1, 1, 1)
            labels_in = labels.repeat(2)
            context_mask = torch.zeros_like(labels_in, dtype=torch.long, device=device)
            context_mask[n_samples:] = 1
            eps = self.nn_model(x_in, labels_in, t_scaled.repeat(2, 1), context_mask)
            eps1, eps2 = eps[:n_samples], eps[n_samples:]
            eps = (1 + guide_weight) * eps1 - guide_weight * eps2
            z = torch.randn_like(x) if i > 1 else 0.0
            x = self.oneover_sqrta[i] * (x - eps * self.mab_over_sqrtmab[i]) + self.sqrt_beta_t[i] * z
        return x, None

