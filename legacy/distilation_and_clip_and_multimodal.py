def enhanced_text_to_image(query, img_enc, txt_enc, pairs, k=5, num_candidates=1000):
    """Enhanced text-to-image retrieval with more candidates"""
    device = next(img_enc.parameters()).device

    with torch.no_grad():
        txt_emb = txt_enc([query])

        # Sample more candidates for better results
        candidates = random.sample(pairs, min(num_candidates, len(pairs)))
        imgs = torch.stack([img for img, _ in candidates]).to(device)

        # Batch process images to avoid memory issues
        batch_size = 100
        all_sims = []

        for i in range(0, len(imgs), batch_size):
            batch_imgs = imgs[i:i+batch_size]
            img_embs = img_enc(batch_imgs)
            sims = (txt_emb @ img_embs.T).squeeze().cpu()
            all_sims.append(sims)

        all_sims = torch.cat(all_sims)
        topk = all_sims.topk(k)

    plt.figure(figsize=(15, 3))
    for i, idx in enumerate(topk.indices):
        plt.subplot(1, k, i+1)
        img = candidates[idx][0]
        if img.dim() == 3:
            img = img.squeeze(0)
        plt.imshow(img.cpu().numpy(), cmap="gray")
        plt.axis("off")
        plt.title(f"Rank {i+1}\nScore: {topk.values[i]:.3f}")
    plt.suptitle(f'Text→Image: "{query}"', fontsize=14, y=1.02)
    plt.tight_layout()
    plt.show()

def enhanced_image_to_text(idx, img_enc, txt_enc, pairs, k=10):
    """Enhanced image-to-text retrieval"""
    device = next(img_enc.parameters()).device
    img, true_txt = pairs[idx]

    with torch.no_grad():
        img_emb = img_enc(img.unsqueeze(0).to(device))

        # Get diverse text candidates
        text_candidates = random.sample([t for _, t in pairs], min(1000, len(pairs)))
        txt_embs = txt_enc(text_candidates)

        sims = (img_emb @ txt_embs.T).squeeze().cpu()
        topk = sims.topk(k)

    # Display image
    plt.figure(figsize=(3, 3))
    if img.dim() == 3:
        img = img.squeeze(0)
    plt.imshow(img.cpu().numpy(), cmap="gray")
    plt.axis("off")
    plt.title("Query Image")
    plt.show()

    # Display results
    console.print(f"[green]True caption:[/green] {true_txt}")
    console.print("[blue]Top predictions:[/blue]")

    results_table = Table()
    results_table.add_column("Rank", style="cyan")
    results_table.add_column("Caption", style="white")
    results_table.add_column("Score", justify="right", style="magenta")

    for i in range(k):
        results_table.add_row(
            f"{i+1}",
            text_candidates[topk.indices[i]],
            f"{topk.values[i]:.3f}"
        )

    console.print(results_table)

