# Inference: compare Teacher vs CNN vs ViT
# ---------------------------
def inference_compare(sample_idx=42, temperature=2.0,
                      teacher_ckpt="vgg16_teacher_20class.pt",
                      cnn_ckpt="cnn_student.pt", vit_ckpt="vit_student.pt"):
    device = "cuda" if torch.cuda.is_available() else "cpu"

    tf = TT.Compose([
        TT.Resize(224, interpolation=TT.InterpolationMode.BICUBIC),
        TT.CenterCrop(224),
        TT.ToTensor(),
        TT.Lambda(lambda x: x.repeat(3,1,1)),
        TT.Normalize((0.485,0.456,0.406),(0.229,0.224,0.225))
    ])
    mnist_t = tv.datasets.MNIST("./data", train=False, download=True, transform=tf)
    fmnist_t = tv.datasets.FashionMNIST("./data", train=False, download=True, transform=tf)
    test_ds = ConcatDataset([mnist_t, fmnist_t])
    x, y = test_ds[sample_idx]
    x = x.unsqueeze(0).to(device)

    # Teacher
    teacher = vgg16(weights=None)
    in_features = teacher.classifier[6].in_features
    teacher.classifier[6] = nn.Linear(in_features, 20)
    teacher.load_state_dict(torch.load(teacher_ckpt, map_location=device))
    teacher = teacher.to(device).eval()
    with torch.no_grad():
        t_probs = F.softmax(teacher(x) / temperature, dim=1)[0].cpu().numpy()

    # CNN student
    cnn = SmallCNN(num_classes=20, rep_dim=128).to(device).eval()
    if os.path.exists(cnn_ckpt):
        cnn.load_state_dict(torch.load(cnn_ckpt, map_location=device))
    with torch.no_grad():
        s_probs = F.softmax(cnn(x)[0] / temperature, dim=1)[0].cpu().numpy()

    # ViT student
    vit = TinyViT(num_classes=20).to(device).eval()
    if os.path.exists(vit_ckpt):
        vit.load_state_dict(torch.load(vit_ckpt, map_location=device))
    with torch.no_grad():
        v_probs = F.softmax(vit(x)[0] / temperature, dim=1)[0].cpu().numpy()

    plt.figure(figsize=(15,4))
    plt.subplot(1,3,1); plt.title("Teacher"); plt.bar(range(len(t_probs)), t_probs)
    plt.subplot(1,3,2); plt.title("CNN Student"); plt.bar(range(len(s_probs)), s_probs)
    plt.subplot(1,3,3); plt.title("ViT Student"); plt.bar(range(len(v_probs)), v_probs)
    plt.tight_layout(); plt.show()
    return t_probs, s_probs, v_probs, y

