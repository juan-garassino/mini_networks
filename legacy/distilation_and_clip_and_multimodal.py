def generate_rich_captions_fmnist():
    templates = [
        "a {descriptor} {item}",
        "an image of a {descriptor} {item}",
        "this is a {descriptor} {item}",
        "a {style} {item}",
        "a picture showing a {descriptor} {item}",
        "{item} with {descriptor} appearance",
        "a clear {item}",
    ]
    descriptors = ["clear","bold","faint","thick","thin","dark","light","printed","stylized","simple","complex"]
    styles = ["stylish","elegant","casual","formal","modern","classic"]
    items = ["t-shirt","trouser","pullover","dress","coat","sandal","shirt","sneaker","bag","ankle boot"]

    cap_map = {i: [] for i in range(10)}
    for i, item in enumerate(items):
        base = [item, f"fashion {item}", f"clothing {item}"]
        for b in base:
            for t in templates:
                if "{descriptor}" in t:
                    for d in descriptors:
                        cap_map[i].append(t.format(descriptor=d, item=b))
                elif "{style}" in t:
                    for s in styles:
                        cap_map[i].append(t.format(style=s, item=b))
                else:
                    cap_map[i].append(t.format(item=b))
    return cap_map, items

class FashionCaptionDataset(Dataset):
    def __init__(self, split="train", max_len=16, img_size=28):
        self.max_len = max_len
        self.img_size = img_size
        self.tfm = T.Compose([T.Resize(img_size), T.ToTensor(), T.Normalize((0.5,), (0.5,))])
        self.ds = tv.datasets.FashionMNIST("./data", train=(split=="train"), download=True, transform=self.tfm)
        self.cap_map, self.item_names = generate_rich_captions_fmnist()
        # Build vocab
        all_caps = []
        for y in range(10):
            all_caps.extend(self.cap_map[y])
        self.vocab = sorted(set(" ".join(all_caps).split()))
        # specials
        self.special = ["<pad>", "<bos>", "<eos>"]
        self.stoi = {w: i+3 for i, w in enumerate(self.vocab)}
        self.stoi["<pad>"] = 0; self.stoi["<bos>"] = 1; self.stoi["<eos>"] = 2
        self.itos = {i: w for w, i in self.stoi.items()}
        self.vocab_size = len(self.stoi)

    def __len__(self): return len(self.ds)
    def _rand_caption(self, y): return random.choice(self.cap_map[int(y)])

    def _tokenize(self, text, add_special=False):
        toks = (["<bos>"] + text.lower().split() + ["<eos>"]) if add_special else text.lower().split()
        ids = [self.stoi.get(t, 0) for t in toks]
        ids = ids[:self.max_len] + [self.stoi["<pad>"]] * (self.max_len - len(ids))
        return torch.tensor(ids, dtype=torch.long)

    def __getitem__(self, idx):
        x, y = self.ds[idx]
        cap = self._rand_caption(y)
        enc_tok = self._tokenize(cap, add_special=False)
        dec_tok = self._tokenize(cap, add_special=True)
        return x, int(y), cap, enc_tok, dec_tok

