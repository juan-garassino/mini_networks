def load_test_data(num_samples=5000):
    """Load test data for inference (separate from training data)"""
    console.print("[blue]Loading test data...[/blue]")

    # Use test split of datasets
    transform = T.Compose([
        T.Resize(32),
        T.ToTensor(),
        T.Normalize((0.5,), (0.5,))
    ])

    # Load test datasets
    mnist_test = tv.datasets.MNIST("./data", train=False, download=True, transform=transform)
    fmnist_test = tv.datasets.FashionMNIST("./data", train=False, download=True, transform=transform)
    emnist_test = tv.datasets.EMNIST("./data", split="letters", train=False, download=True, transform=transform)

    # Generate captions (same as training)
    mnist_captions, fmnist_captions, emnist_captions = generate_rich_captions()

    # Create test pairs
    test_pairs = []

    # MNIST test pairs
    for i, (img, y) in enumerate(mnist_test):
        if i >= num_samples // 3:
            break
        class_captions = [cap for cls, cap in mnist_captions if cls == y]
        caption = random.choice(class_captions)
        test_pairs.append((img, caption))

    # FashionMNIST test pairs
    for i, (img, y) in enumerate(fmnist_test):
        if i >= num_samples // 3:
            break
        class_captions = [cap for cls, cap in fmnist_captions if cls == y]
        caption = random.choice(class_captions)
        test_pairs.append((img, caption))

    # EMNIST test pairs
    for i, (img, y) in enumerate(emnist_test):
        if i >= num_samples // 3:
            break
        class_captions = [cap for cls, cap in emnist_captions if cls == y]
        if class_captions:
            caption = random.choice(class_captions)
            test_pairs.append((img, caption))
        else:
            if 1 <= y <= 26:
                letter = chr(y + 96)
                caption = f"letter {letter}"
                test_pairs.append((img, caption))

    console.print(f"[green]✓ Loaded {len(test_pairs)} test samples[/green]")
    return test_pairs

