def train_joint(epochs=2, batch_size=256, emb_dim=128, ddpm_steps=200,
                lam_clip=1.0, lam_cap=1.0, lam_diff=1.0, lr=2e-4, max_len=16):

    # Build dataset & loaders
    train_ds = FashionCaptionDataset(split="train", max_len=max_len)
    val_ds   = FashionCaptionDataset(split="test",  max_len=max_len)  # using test as val
    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True, num_workers=2, drop_last=True)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size, shuffle=False, num_workers=2, drop_last=True)

    model = NanoMultimodal(vocab_size=train_ds.vocab_size, emb_dim=emb_dim, max_len=max_len, ddpm_steps=ddpm_steps).to(DEVICE)
    opt = torch.optim.AdamW(model.parameters(), lr=lr, weight_decay=1e-4)
    sched = torch.optim.lr_scheduler.CosineAnnealingLR(opt, T_max=epochs*len(train_loader))

    print(Fore.MAGENTA + f"\n[ Joint Training ] epochs={epochs} bs={batch_size} emb={emb_dim} steps={ddpm_steps}" + Style.RESET_ALL)
    best_val = float("inf")

    for ep in range(1, epochs+1):
        model.train()
        pbar = tqdm(train_loader, desc=f"[Train] Epoch {ep}/{epochs}")
        loss_m = {"total":0.0,"clip":0.0,"cap":0.0,"diff":0.0}
        for imgs, ys, caps, enc_tok, dec_tok in pbar:
            imgs = imgs.to(DEVICE)
            enc_tok = enc_tok.to(DEVICE)
            dec_tok = dec_tok.to(DEVICE)

            out = model.forward_losses(imgs, enc_tok, dec_tok,
                                       lam_clip=lam_clip, lam_cap=lam_cap, lam_diff=lam_diff)
            opt.zero_grad(); out["total"].backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            opt.step(); sched.step()

            for k in ["total","clip","cap","diff"]:
                loss_m[k] += out[k].item()
            pbar.set_postfix(total=f"{out['total'].item():.3f}",
                             clip=f"{out['clip'].item():.3f}",
                             cap=f"{out['cap'].item():.3f}",
                             diff=f"{out['diff'].item():.3f}",
                             temp=f"{out['temp'].item():.3f}")
        nbt = len(train_loader)
        print(Fore.CYAN + f"  train: total={loss_m['total']/nbt:.3f} "
                          f"clip={loss_m['clip']/nbt:.3f} cap={loss_m['cap']/nbt:.3f} diff={loss_m['diff']/nbt:.3f}" + Style.RESET_ALL)

        # --- validation (light, no sampling here)
        model.eval()
        vloss = 0.0
        with torch.no_grad():
            for imgs, ys, caps, enc_tok, dec_tok in val_loader:
                imgs, enc_tok, dec_tok = imgs.to(DEVICE), enc_tok.to(DEVICE), dec_tok.to(DEVICE)
                out = model.forward_losses(imgs, enc_tok, dec_tok,
                                           lam_clip=lam_clip, lam_cap=lam_cap, lam_diff=lam_diff)
                vloss += out["total"].item()
        vloss /= len(val_loader)
        print(Fore.YELLOW + f"  val:   total={vloss:.3f}" + Style.RESET_ALL)

        if vloss < best_val:
            best_val = vloss
            torch.save({"model": model.state_dict(),
                        "meta": {"vocab": train_ds.vocab, "stoi": train_ds.stoi, "itos": train_ds.itos,
                                 "emb_dim": emb_dim, "max_len": max_len, "ddpm_steps": ddpm_steps}},
                       "nano_multimodal_joint.pt")
            print(Fore.GREEN + "✓ Saved best checkpoint: nano_multimodal_joint.pt" + Style.RESET_ALL)

    return model, train_ds, val_ds

