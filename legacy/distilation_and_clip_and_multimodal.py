# 5) Merged Multimodal Model (pixel or latent)
# ===============================

class NanoMultimodal(nn.Module):
    def __init__(self, vocab_size, emb_dim=128, max_len=16,
                 steps=200, drop_prob=0.1, use_latent=False, latent_ch=4, predict_score=False):
        super().__init__()
        self.use_latent = use_latent
        self.img_enc = ImageEncoder(emb_dim)
        self.txt_enc = TextEncoder(vocab_size, emb_dim, max_len=max_len)
        self.cap_dec = CaptionDecoder(vocab_size, d_model=emb_dim, max_len=max_len)
        self.pad_id = 0
        self.log_temp = nn.Parameter(torch.log(torch.tensor(0.07)))

        if use_latent:
            self.vae = VAE(latent_channels=latent_ch)   # will be trained separately then frozen
            self.unet = ContextUNetVecLatent(in_ch=latent_ch, n_feat=emb_dim, cond_dim=emb_dim)
            self.diff = LDMVec(self.unet, steps=steps, drop_prob=drop_prob, device=DEVICE, predict_score=predict_score)
        else:
            self.unet = ContextUNetVecPixel(in_ch=1, n_feat=emb_dim, cond_dim=emb_dim)
            self.diff = DDPMVec(self.unet, steps=steps, drop_prob=drop_prob, device=DEVICE, predict_score=predict_score)

    # ---- losses
    def contrastive_loss(self, img_emb, txt_emb):
        temp = self.log_temp.exp()
        logits = img_emb @ txt_emb.T / temp
        labels = torch.arange(img_emb.size(0), device=img_emb.device)
        return (F.cross_entropy(logits, labels) + F.cross_entropy(logits.T, labels))/2, temp

    def caption_loss(self, img_emb, dec_tok):
        y_inp, y_tgt = dec_tok[:, :-1], dec_tok[:, 1:]
        logits = self.cap_dec(img_emb, y_inp)
        return F.cross_entropy(logits.reshape(-1, logits.size(-1)), y_tgt.reshape(-1), ignore_index=self.pad_id)

    def diffusion_loss(self, imgs, txt_emb):
        if self.use_latent:
            with torch.no_grad():
                mu, _ = self.vae.encode(imgs)
                z0 = mu
            return self.diff(z0, txt_emb)
        else:
            return self.diff(imgs, txt_emb)

    def forward_losses(self, imgs, enc_tok, dec_tok, lam_clip=1.0, lam_cap=1.0, lam_diff=1.0):
        img_emb = self.img_enc(imgs)
        txt_emb = self.txt_enc(enc_tok)
        l_clip, temp = self.contrastive_loss(img_emb, txt_emb)
        l_cap = self.caption_loss(img_emb, dec_tok)
        l_diff = self.diffusion_loss(imgs, txt_emb)
        total = lam_clip*l_clip + lam_cap*l_cap + lam_diff*l_diff
        return {"total": total, "clip": l_clip, "cap": l_cap, "diff": l_diff, "temp": temp,
                "img_emb": img_emb.detach(), "txt_emb": txt_emb.detach()}

