def generate_rich_captions():
    templates = [
        "a {descriptor} {item}",
        "an image of a {descriptor} {item}",
        "this is a {descriptor} {item}",
        "a {style} {item}",
        "a picture showing a {descriptor} {item}",
        "{item} with {descriptor} appearance",
        "handwritten {item}",
        "a clear {item}",
    ]
    descriptors = ["clear","bold","faint","thick","thin","dark","light","handwritten","printed","stylized","simple","complex"]
    styles = ["stylish","elegant","casual","formal","modern","classic"]

    mnist_caps = []
    for i in range(10):
        base_items = [f"digit {i}", f"number {i}", f"numeral {i}"]
        for item in base_items:
            for t in templates:
                if "{descriptor}" in t:
                    for d in descriptors:
                        mnist_caps.append((i, t.format(descriptor=d, item=item)))
                elif "{style}" in t:
                    for s in styles:
                        mnist_caps.append((i, t.format(style=s, item=item)))
                else:
                    mnist_caps.append((i, t.format(item=item)))

    fitems = ["t-shirt","trouser","pullover","dress","coat","sandal","shirt","sneaker","bag","ankle boot"]
    fmnist_caps = []
    for i, item in enumerate(fitems):
        base_items = [item, f"fashion {item}", f"clothing {item}"]
        for bi in base_items:
            for t in templates:
                if "{descriptor}" in t:
                    for d in descriptors + styles:
                        fmnist_caps.append((i, t.format(descriptor=d, item=bi)))
                elif "{style}" in t:
                    for s in styles:
                        fmnist_caps.append((i, t.format(style=s, item=bi)))
                else:
                    fmnist_caps.append((i, t.format(item=bi)))
    return mnist_caps, fmnist_caps

class EnhancedImageEncoder(nn.Module):
    def __init__(self, emb_dim=128):
        super().__init__()
        self.cnn = nn.Sequential(
            nn.Conv2d(1, 32, 3, 2, 1), nn.BatchNorm2d(32), nn.ReLU(),
            nn.Conv2d(32,64, 3, 2, 1), nn.BatchNorm2d(64), nn.ReLU(),
            nn.Conv2d(64,128,3, 2, 1), nn.BatchNorm2d(128), nn.ReLU(),
            nn.Conv2d(128,256,3, 2, 1), nn.BatchNorm2d(256), nn.ReLU(),
            nn.AdaptiveAvgPool2d(1), nn.Dropout(0.1)
        )
        self.fc = nn.Sequential(
            nn.Linear(256, emb_dim), nn.ReLU(), nn.Linear(emb_dim, emb_dim)
        )
    def forward(self, x):
        h = self.cnn(x).view(x.size(0), -1)
        z = self.fc(h)
        return F.normalize(z, dim=-1)

class EnhancedTextEncoder(nn.Module):
    def __init__(self, vocab, emb_dim=128, n_heads=4, n_layers=2, max_len=15):
        super().__init__()
        self.vocab = vocab
        self.stoi = {w: i+1 for i, w in enumerate(vocab)}  # 0=pad
        self.vocab_size = len(self.stoi) + 1
        self.max_len = max_len
        self.embed = nn.Embedding(self.vocab_size, emb_dim, padding_idx=0)
        self.pos_embed = nn.Parameter(torch.randn(1, max_len, emb_dim) * 0.01)
        self.dropout = nn.Dropout(0.1)
        layer = nn.TransformerEncoderLayer(d_model=emb_dim, nhead=n_heads, dim_feedforward=emb_dim*4,
                                           batch_first=True, norm_first=True, dropout=0.1)
        self.encoder = nn.TransformerEncoder(layer, num_layers=n_layers)
        self.fc = nn.Sequential(nn.Linear(emb_dim, emb_dim), nn.ReLU(), nn.Linear(emb_dim, emb_dim))

    def tokenize(self, text):
        toks = [self.stoi.get(w.lower(), 0) for w in text.split()]
        toks = toks[:self.max_len] + [0] * (self.max_len - len(toks))
        return torch.tensor(toks, dtype=torch.long)

    def forward(self, texts):
        device = self.embed.weight.device
        toks = torch.stack([self.tokenize(t) for t in texts]).to(device)
        mask = (toks == 0)
        x = self.embed(toks) + self.pos_embed[:, :toks.size(1)]
        x = self.dropout(x)
        h = self.encoder(x, src_key_padding_mask=mask)
        mask_expanded = mask.unsqueeze(-1).expand_as(h)
        h_masked = h.masked_fill(mask_expanded, 0)
        pooled = h_masked.sum(dim=1) / (~mask).sum(dim=1, keepdim=True).float().clamp(min=1)
        z = self.fc(pooled)
        return F.normalize(z, dim=-1)

class ContrastiveLoss(nn.Module):
    def __init__(self, init_temp=0.07):
        super().__init__()
        self.log_temp = nn.Parameter(torch.log(torch.tensor(init_temp)))
    def forward(self, img_emb, txt_emb):
        temperature = self.log_temp.exp()
        logits = img_emb @ txt_emb.T / temperature
        labels = torch.arange(len(img_emb), device=img_emb.device)
        loss_i = F.cross_entropy(logits, labels)
        loss_t = F.cross_entropy(logits.T, labels)
        return (loss_i + loss_t)/2, temperature

def build_enhanced_loaders_for_clip(batch_size=256, val_split=0.1):
    train_tf = T.Compose([
        T.Resize(32),
        T.RandomRotation(10),
        T.RandomAffine(degrees=0, translate=(0.1,0.1)),
        T.ToTensor(),
        T.Normalize((0.5,), (0.5,))
    ])
    val_tf = T.Compose([
        T.Resize(32), T.ToTensor(), T.Normalize((0.5,), (0.5,))
    ])

    mnist_caps, fmnist_caps = generate_rich_captions()
    mnist = tv.datasets.MNIST("./data", train=True, download=True, transform=train_tf)
    fmnist = tv.datasets.FashionMNIST("./data", train=True, download=True, transform=train_tf)

    pairs = []
    for img, y in mnist:
        caps = [cap for cls, cap in mnist_caps if cls == y]
        caption = random.choice(caps)
        pairs.append((img, caption))
    for img, y in fmnist:
        caps = [cap for cls, cap in fmnist_caps if cls == y]
        caption = random.choice(caps)
        pairs.append((img, caption))

    random.shuffle(pairs)
    split_idx = int(len(pairs) * (1 - val_split))
    train_pairs, val_pairs = pairs[:split_idx], pairs[split_idx:]

    class CLIPDataset(torch.utils.data.Dataset):
        def __init__(self, pairs): self.pairs = pairs
        def __len__(self): return len(self.pairs)
        def __getitem__(self, idx): return self.pairs[idx]

    train_ds = CLIPDataset(train_pairs)
    val_ds = CLIPDataset(val_pairs)

    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True, num_workers=2)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size, shuffle=False, num_workers=2)

    all_caps = [cap for _, cap in pairs]
    vocab = sorted(set(' '.join(all_caps).split()))
    return train_loader, val_loader, vocab, train_pairs, val_pairs

def train_nanoclip(epochs=2, batch_size=256, emb_dim=128, lr=1e-3):
    train_loader, val_loader, vocab, train_pairs, val_pairs = build_enhanced_loaders_for_clip(batch_size)
    img_enc = EnhancedImageEncoder(emb_dim).to(DEVICE)
    txt_enc = EnhancedTextEncoder(vocab, emb_dim).to(DEVICE)
    crit    = ContrastiveLoss().to(DEVICE)
    opt = torch.optim.AdamW(list(img_enc.parameters()) + list(txt_enc.parameters()) + list(crit.parameters()),
                            lr=lr, weight_decay=1e-4)
    sched = torch.optim.lr_scheduler.CosineAnnealingLR(opt, epochs * len(train_loader))
    best_val = float('inf')
    for ep in range(1, epochs+1):
        img_enc.train(); txt_enc.train()
        train_loss = 0
        pbar = tqdm(train_loader, desc=f"[NanoCLIP] Epoch {ep}/{epochs}")
        for imgs, texts in pbar:
            imgs = imgs.to(DEVICE)
            img_emb = img_enc(imgs)
            txt_emb = txt_enc(texts)
            loss, temp = crit(img_emb, txt_emb)
            opt.zero_grad(); loss.backward()
            torch.nn.utils.clip_grad_norm_(list(img_enc.parameters())+list(txt_enc.parameters()), 1.0)
            opt.step(); sched.step()
            train_loss += loss.item()
            pbar.set_postfix(loss=f"{loss.item():.4f}", temp=f"{temp.item():.3f}")
        img_enc.eval(); txt_enc.eval()
        val_loss = 0
        with torch.no_grad():
            for imgs, texts in val_loader:
                imgs = imgs.to(DEVICE)
                loss, _ = crit(img_enc(imgs), txt_enc(texts))
                val_loss += loss.item()
        val_loss /= max(1, len(val_loader))
        print(Fore.CYAN + f"[NanoCLIP] val_loss={val_loss:.4f}" + Style.RESET_ALL)
        if val_loss < best_val:
            best_val = val_loss
            torch.save({"img":img_enc.state_dict(),"txt":txt_enc.state_dict(),"vocab":vocab,"config":{"emb_dim":emb_dim}},
                       "nanoclip_enhanced.pt")
            print(Fore.GREEN + "✓ Saved best NanoCLIP" + Style.RESET_ALL)
    return img_enc, txt_enc, vocab, train_pairs, val_pairs

def load_nanoclip(model_path="nanoclip_enhanced.pt"):
    ckpt = torch.load(model_path, map_location=DEVICE)
    vocab = ckpt["vocab"]; emb_dim = ckpt["config"]["emb_dim"]
    img_enc = EnhancedImageEncoder(emb_dim).to(DEVICE)
    txt_enc = EnhancedTextEncoder(vocab, emb_dim).to(DEVICE)
    img_enc.load_state_dict(ckpt["img"]); txt_enc.load_state_dict(ckpt["txt"])
    img_enc.eval(); txt_enc.eval()
    return img_enc, txt_enc, vocab

