class ImageEncoder(nn.Module):
    def __init__(self, emb_dim=128):
        super().__init__()
        self.cnn = nn.Sequential(
            nn.Conv2d(1, 32, 3, 2, 1), nn.BatchNorm2d(32), nn.ReLU(),
            nn.Conv2d(32,64, 3, 2, 1), nn.BatchNorm2d(64), nn.ReLU(),
            nn.Conv2d(64,128,3, 2, 1), nn.BatchNorm2d(128), nn.ReLU(),
            nn.AdaptiveAvgPool2d(1), nn.Dropout(0.1)
        )
        self.fc = nn.Sequential(nn.Linear(128, emb_dim), nn.ReLU(), nn.Linear(emb_dim, emb_dim))
    def forward(self, x):
        h = self.cnn(x).view(x.size(0), -1)
        z = self.fc(h)
        return F.normalize(z, dim=-1)

class TextEncoder(nn.Module):
    def __init__(self, vocab_size, emb_dim=128, max_len=16, n_heads=4, n_layers=2):
        super().__init__()
        self.pad_id = 0
        self.embed = nn.Embedding(vocab_size, emb_dim, padding_idx=0)
        self.pos = nn.Parameter(torch.randn(1, max_len, emb_dim) * 0.01)
        layer = nn.TransformerEncoderLayer(d_model=emb_dim, nhead=n_heads, dim_feedforward=emb_dim*4,
                                           batch_first=True, norm_first=True, dropout=0.1)
        self.enc = nn.TransformerEncoder(layer, num_layers=n_layers)
        self.proj = nn.Sequential(nn.Linear(emb_dim, emb_dim), nn.ReLU(), nn.Linear(emb_dim, emb_dim))
    def forward(self, tok_no_special):
        mask = (tok_no_special == self.pad_id)
        x = self.embed(tok_no_special) + self.pos[:, :tok_no_special.size(1)]
        h = self.enc(x, src_key_padding_mask=mask)
        h = h.masked_fill(mask.unsqueeze(-1), 0)
        denom = (~mask).sum(dim=1, keepdim=True).clamp(min=1).float()
        pooled = h.sum(dim=1) / denom
        z = self.proj(pooled)
        return F.normalize(z, dim=-1)

class CaptionDecoder(nn.Module):
    def __init__(self, vocab_size, d_model=128, max_len=16, n_heads=4, n_layers=2):
        super().__init__()
        self.pad_id = 0; self.bos_id = 1; self.eos_id = 2; self.max_len = max_len
        self.tok = nn.Embedding(vocab_size, d_model, padding_idx=0)
        self.pos = nn.Parameter(torch.randn(1, max_len, d_model) * 0.01)
        layer = nn.TransformerDecoderLayer(d_model=d_model, nhead=n_heads, dim_feedforward=d_model*4,
                                           batch_first=True, norm_first=True, dropout=0.1)
        self.dec = nn.TransformerDecoder(layer, num_layers=n_layers)
        self.mem_proj = nn.Linear(d_model, d_model)
        self.out = nn.Linear(d_model, vocab_size)
    def forward(self, memory, y_inp):
        B, L = y_inp.shape
        tgt = self.tok(y_inp) + self.pos[:, :L]
        causal = torch.triu(torch.ones(L, L, device=y_inp.device), diagonal=1).bool()
        mem = self.mem_proj(memory).unsqueeze(1)
        h = self.dec(tgt, mem, tgt_mask=causal)
        return self.out(h)
    @torch.no_grad()
    def greedy(self, memory, max_len=16):
        B = memory.size(0)
        y = torch.full((B,1), self.bos_id, dtype=torch.long, device=memory.device)
        for _ in range(max_len-1):
            logits = self.forward(memory, y)[:, -1, :]
            nxt = logits.argmax(dim=-1, keepdim=True)
            y = torch.cat([y, nxt], dim=1)
            if (nxt == self.eos_id).all(): break
        return y

