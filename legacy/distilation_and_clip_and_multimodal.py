def train_vae(epochs=2, batch_size=128, lr=1e-3, img_size=32):
    ds = FashionCaptionDataset(split="train", img_size=img_size)
    loader = DataLoader(ds, batch_size=batch_size, shuffle=True, num_workers=2, drop_last=True)
    vae = VAE().to(DEVICE)
    opt = torch.optim.Adam(vae.parameters(), lr=lr)
    best = float('inf')

    for ep in range(1, epochs+1):
        vae.train(); loss_ema = None
        pbar = tqdm(loader, desc=f"[VAE] Epoch {ep}/{epochs}")
        for imgs, *_ in pbar:
            imgs = imgs.to(DEVICE)  # [B,1,32,32]
            recon, mu, logvar = vae(imgs)
            assert recon.shape == imgs.shape, f"recon {recon.shape} vs imgs {imgs.shape}"

            recon_loss = F.mse_loss(recon, imgs, reduction="mean")
            kl = -0.5 * (1 + logvar - mu.pow(2) - logvar.exp()).mean()
            loss = recon_loss + 0.001 * kl

            opt.zero_grad(); loss.backward(); opt.step()

            loss_ema = loss.item() if loss_ema is None else 0.95 * loss_ema + 0.05 * loss.item()
            pbar.set_postfix(loss=f"{loss_ema:.3f}")

        if loss_ema < best:
            best = loss_ema
            torch.save(vae.state_dict(), "vae.pt")
            print(Fore.GREEN + "✓ Saved best VAE" + Style.RESET_ALL)

    vae.eval()
    return vae


def train_joint(epochs=2, batch_size=256, emb_dim=128, steps=200,
                lam_clip=1.0, lam_cap=1.0, lam_diff=1.0, lam_lm=1.0,
                lr=2e-4, max_len=16, use_latent=False, latent_ch=4, predict_score=False,
                vae=None, img_size_pixel=28, img_size_latent=32, subset=None):

    img_size = img_size_latent if use_latent else img_size_pixel
    train_ds = FashionCaptionDataset(split="train", max_len=max_len, img_size=img_size)
    if subset and subset < len(train_ds):
        idx = torch.randperm(len(train_ds))[:subset].tolist()
        train_ds = Subset(train_ds, idx)
    val_ds   = FashionCaptionDataset(split="test", max_len=max_len, img_size=img_size)
    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True, num_workers=2, drop_last=True)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size, shuffle=False, num_workers=2, drop_last=True)

    model = NanoMultimodal(vocab_size=(train_ds.dataset.vocab_size if isinstance(train_ds, Subset) else train_ds.vocab_size),
                           emb_dim=emb_dim, max_len=max_len, steps=steps, use_latent=use_latent,
                           latent_ch=latent_ch, predict_score=predict_score).to(DEVICE)

    if use_latent:
        assert vae is not None, "VAE must be provided when use_latent=True (two-stage training)."
        model.vae.load_state_dict(vae.state_dict())
        model.vae.eval()
        for p in model.vae.parameters(): p.requires_grad = False

    opt = torch.optim.AdamW(filter(lambda p: p.requires_grad, model.parameters()), lr=lr, weight_decay=1e-4)
    sched = torch.optim.lr_scheduler.CosineAnnealingLR(opt, T_max=epochs*len(train_loader))

    print(Fore.MAGENTA + f"\n[ Joint Training ] mode={'LATENT' if use_latent else 'PIXEL'} | epochs={epochs} steps={steps}" + Style.RESET_ALL)
    best_val = float("inf")

    for ep in range(1, epochs+1):
        model.train()
        pbar = tqdm(train_loader, desc=f"[Train] Epoch {ep}/{epochs}")
        agg = {"total":0,"clip":0,"cap":0,"diff":0,"lm":0}
        for batch in pbar:
            imgs, ys, caps, enc_tok, dec_tok = batch
            imgs, enc_tok, dec_tok = imgs.to(DEVICE), enc_tok.to(DEVICE), dec_tok.to(DEVICE)
            out = model.forward_losses(imgs, enc_tok, dec_tok,
                                       lam_clip=lam_clip, lam_cap=lam_cap, lam_diff=lam_diff, lam_lm=lam_lm)
            opt.zero_grad(); out["total"].backward()
            torch.nn.utils.clip_grad_norm_(filter(lambda p: p.requires_grad, model.parameters()), 1.0)
            opt.step(); sched.step()
            for k in agg: agg[k] += out[k].item()
            pbar.set_postfix(total=f"{out['total'].item():.3f}",
                             clip=f"{out['clip'].item():.3f}",
                             cap=f"{out['cap'].item():.3f}",
                             diff=f"{out['diff'].item():.3f}",
                             lm=f"{out['lm'].item():.3f}",
                             temp=f"{out['temp'].item():.3f}")

        nbt = len(train_loader)
        print(Fore.CYAN + f"  train avg: total={agg['total']/nbt:.3f} clip={agg['clip']/nbt:.3f} cap={agg['cap']/nbt:.3f} diff={agg['diff']/nbt:.3f} lm={agg['lm']/nbt:.3f}" + Style.RESET_ALL)

        model.eval(); vloss=0.0
        with torch.no_grad():
            for imgs, ys, caps, enc_tok, dec_tok in val_loader:
                imgs, enc_tok, dec_tok = imgs.to(DEVICE), enc_tok.to(DEVICE), dec_tok.to(DEVICE)
                out = model.forward_losses(imgs, enc_tok, dec_tok,
                                           lam_clip=lam_clip, lam_cap=lam_cap, lam_diff=lam_diff, lam_lm=lam_lm)
                vloss += out["total"].item()
        vloss /= len(val_loader)
        print(Fore.YELLOW + f"  val avg: total={vloss:.3f}" + Style.RESET_ALL)

        if vloss < best_val:
            best_val = vloss
            torch.save({
                "model": model.state_dict(),
                "meta": {
                    "use_latent": use_latent, "emb_dim": emb_dim, "max_len": max_len, "steps": steps,
                    "latent_ch": latent_ch, "predict_score": predict_score,
                    "vocab": (train_ds.dataset.vocab if isinstance(train_ds, Subset) else train_ds.vocab),
                    "stoi":  (train_ds.dataset.stoi  if isinstance(train_ds, Subset) else train_ds.stoi),
                    "itos":  (train_ds.dataset.itos  if isinstance(train_ds, Subset) else train_ds.itos),
                },
                "vae": (vae.state_dict() if use_latent else None)
            }, "nano_multimodal_merged.pt")
            print(Fore.GREEN + "✓ Saved best joint checkpoint: nano_multimodal_merged.pt" + Style.RESET_ALL)

    return model, (train_ds.dataset if isinstance(train_ds, Subset) else train_ds), val_ds

