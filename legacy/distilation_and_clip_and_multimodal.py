@torch.no_grad()
def text_to_image(model, text, stoi, max_len=16, n=8, guide_w=2.0):
    # tokenize for encoder (no specials)
    toks = text.lower().split()
    ids = [stoi.get(t, 0) for t in toks]
    ids = ids[:max_len] + [stoi["<pad>"]] * (max_len - len(ids))
    enc_tok = torch.tensor(ids, dtype=torch.long, device=DEVICE).unsqueeze(0)
    txt_emb = model.txt_enc(enc_tok)  # (1,d)
    imgs = model.ddpm.sample(n=n, size=(1,28,28), c_vec=txt_emb, guide_w=guide_w)
    show_images(imgs, title=f'Text→Image: "{text}"', nrow=min(8,n), value_range=(-1,1))
    return imgs

@torch.no_grad()
def image_to_text(model, imgs, itos):
    if imgs.ndim == 3: imgs = imgs.unsqueeze(0)
    imgs = imgs.to(DEVICE)
    emb = model.img_enc(imgs)
    seq = model.cap_dec.greedy(emb, max_len=model.cap_dec.max_len)
    texts = []
    for row in seq:
        toks = []
        for tid in row.tolist():
            if tid in (model.cap_dec.bos_id, model.cap_dec.eos_id, model.cap_dec.pad_id): continue
            toks.append(itos.get(tid, ""))
        texts.append(" ".join([t for t in toks if t]))
    return texts

