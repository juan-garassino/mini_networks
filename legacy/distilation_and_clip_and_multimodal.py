class ResidualConvBlock(nn.Module):
    def __init__(self, in_ch, out_ch, is_res=False):
        super().__init__()
        self.is_res = is_res; self.same = in_ch == out_ch
        self.c1 = nn.Sequential(nn.Conv2d(in_ch, out_ch, 3, 1, 1), nn.BatchNorm2d(out_ch), nn.GELU())
        self.c2 = nn.Sequential(nn.Conv2d(out_ch, out_ch, 3, 1, 1), nn.BatchNorm2d(out_ch), nn.GELU())
    def forward(self, x):
        if self.is_res:
            x1 = self.c1(x); x2 = self.c2(x1)
            out = (x + x2) if self.same else (x1 + x2)
            return out / math.sqrt(2)
        else:
            return self.c2(self.c1(x))

class UnetDown(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.seq = nn.Sequential(ResidualConvBlock(in_ch, out_ch), nn.MaxPool2d(2))
    def forward(self, x): return self.seq(x)

# --- FIXED UnetUp: explicit up_in_ch, skip_ch, out_ch ---
class UnetUp(nn.Module):
    def __init__(self, up_in_ch, skip_ch, out_ch):
        super().__init__()
        self.up = nn.ConvTranspose2d(up_in_ch, out_ch, kernel_size=2, stride=2)
        self.res1 = ResidualConvBlock(out_ch + skip_ch, out_ch)
        self.res2 = ResidualConvBlock(out_ch, out_ch)

    def forward(self, x, skip):
        x = self.up(x)
        if x.shape[-2:] != skip.shape[-2:]:
            x = F.interpolate(x, size=skip.shape[-2:], mode="nearest")
        x = torch.cat([x, skip], dim=1)
        x = self.res1(x)
        x = self.res2(x)
        return x


class EmbedFC(nn.Module):
    def __init__(self, in_dim, out_dim):
        super().__init__()
        self.net = nn.Sequential(nn.Linear(in_dim, out_dim), nn.GELU(), nn.Linear(out_dim, out_dim))
    def forward(self, x): return self.net(x)

# --- Pixel U-Net (28x28) with proper skip wiring + conditioning ---
class ContextUNetVecPixel(nn.Module):
    def __init__(self, in_ch=1, n_feat=128, cond_dim=128):
        super().__init__()
        self.n_feat = n_feat
        # encoder
        self.init = ResidualConvBlock(in_ch, n_feat, is_res=True)     # 28x28, n_feat
        self.d1   = UnetDown(n_feat, n_feat)                          # 28→14, n_feat
        self.d2   = UnetDown(n_feat, 2*n_feat)                        # 14→7,  2*n_feat

        # time/context embeddings (two scales)
        self.temb1 = EmbedFC(1, 2*n_feat)   # deep (matches d2 channels)
        self.temb2 = EmbedFC(1, n_feat)     # mid  (matches u1 channels)
        self.cemb1 = EmbedFC(cond_dim, 2*n_feat)
        self.cemb2 = EmbedFC(cond_dim, n_feat)

        # decoder: d2→(up,concat d1)→(up,concat x0)
        self.u1 = UnetUp(up_in_ch=2*n_feat, skip_ch=n_feat, out_ch=n_feat)  # 7→14
        self.u2 = UnetUp(up_in_ch=n_feat,   skip_ch=n_feat, out_ch=n_feat)  # 14→28

        self.out = nn.Sequential(
            nn.Conv2d(2*n_feat, n_feat, 3, 1, 1),
            nn.GroupNorm(8, n_feat), nn.ReLU(),
            nn.Conv2d(n_feat, in_ch, 3, 1, 1),
        )

    def forward(self, x, c_vec, t_scaled, context_mask):
        B = x.size(0)
        x0 = self.init(x)     # [B, n_feat, 28,28]
        d1 = self.d1(x0)      # [B, n_feat, 14,14]
        d2 = self.d2(d1)      # [B, 2*n_feat, 7,7]

        # classifier-free: mask context and make embeddings
        c_masked = c_vec * (1.0 - context_mask.float().unsqueeze(-1))
        c1 = self.cemb1(c_masked).view(B, 2*self.n_feat, 1, 1)
        t1 = self.temb1(t_scaled).view(B, 2*self.n_feat, 1, 1)
        c2 = self.cemb2(c_masked).view(B, self.n_feat, 1, 1)
        t2 = self.temb2(t_scaled).view(B, self.n_feat, 1, 1)

        d2 = d2 + c1 + t1                 # deep (7x7, 2*n_feat)
        u1 = self.u1(d2, d1)              # → 14x14
        u1 = u1 + c2 + t2                 # mid (14x14, n_feat)
        u2 = self.u2(u1, x0)              # → 28x28

        return self.out(torch.cat([u2, x0], dim=1))  # [B, in_ch, 28,28]



# --- Latent U-Net (8x8 latent, C=4) with proper skip wiring + conditioning ---
class ContextUNetVecLatent(nn.Module):
    def __init__(self, in_ch=4, n_feat=128, cond_dim=128):
        super().__init__()
        self.n_feat = n_feat
        # encoder on 8x8
        self.init = ResidualConvBlock(in_ch, n_feat, is_res=True)     # 8x8,  n_feat
        self.d1   = UnetDown(n_feat, n_feat)                          # 8→4,  n_feat
        self.d2   = UnetDown(n_feat, 2*n_feat)                        # 4→2,  2*n_feat

        # time/context embeddings
        self.temb1 = EmbedFC(1, 2*n_feat)   # deep (matches d2)
        self.temb2 = EmbedFC(1, n_feat)     # mid  (matches u1)
        self.cemb1 = EmbedFC(cond_dim, 2*n_feat)
        self.cemb2 = EmbedFC(cond_dim, n_feat)

        # decoder path: d2→(up,concat d1)→(up,concat x0)
        self.u1 = UnetUp(up_in_ch=2*n_feat, skip_ch=n_feat, out_ch=n_feat)  # 2→4
        self.u2 = UnetUp(up_in_ch=n_feat,   skip_ch=n_feat, out_ch=n_feat)  # 4→8

        self.out = nn.Sequential(
            nn.Conv2d(2*n_feat, n_feat, 3, 1, 1),
            nn.GroupNorm(8, n_feat), nn.ReLU(),
            nn.Conv2d(n_feat, in_ch, 3, 1, 1),
        )

    def forward(self, x, c_vec, t_scaled, context_mask):
        B = x.size(0)
        x0 = self.init(x)     # [B, n_feat, 8,8]
        d1 = self.d1(x0)      # [B, n_feat, 4,4]
        d2 = self.d2(d1)      # [B, 2*n_feat, 2,2]

        c_masked = c_vec * (1.0 - context_mask.float().unsqueeze(-1))
        c1 = self.cemb1(c_masked).view(B, 2*self.n_feat, 1, 1)
        t1 = self.temb1(t_scaled).view(B, 2*self.n_feat, 1, 1)
        c2 = self.cemb2(c_masked).view(B, self.n_feat, 1, 1)
        t2 = self.temb2(t_scaled).view(B, self.n_feat, 1, 1)

        d2 = d2 + c1 + t1                 # 2x2
        u1 = self.u1(d2, d1)              # → 4x4
        u1 = u1 + c2 + t2                 # 4x4
        u2 = self.u2(u1, x0)              # → 8x8

        return self.out(torch.cat([u2, x0], dim=1))  # [B, in_ch, 8,8]



def ddpm_schedules(beta1: float, beta2: float, T: int):
    beta_t = (beta2 - beta1) * torch.arange(0, T + 1, dtype=torch.float32) / T + beta1
    sqrt_beta_t = torch.sqrt(beta_t)
    alpha_t = 1.0 - beta_t
    alphabar_t = torch.cumsum(torch.log(alpha_t), dim=0).exp()
    sqrtab = torch.sqrt(alphabar_t)
    oneover_sqrta = 1.0 / torch.sqrt(alpha_t)
    sqrtmab = torch.sqrt(1.0 - alphabar_t)
    mab_over_sqrtmab = (1.0 - alpha_t) / torch.clamp(sqrtmab, min=1e-12)
    return {"alpha_t":alpha_t,"oneover_sqrta":oneover_sqrta,"sqrt_beta_t":sqrt_beta_t,
            "alphabar_t":alphabar_t,"sqrtab":sqrtab,"sqrtmab":sqrtmab,"mab_over_sqrtmab":mab_over_sqrtmab}

# --- Pixel DDPM wrapper (unchanged forward/sample; ADDED q_sample + sample_i2i) ---
class DDPMVec(nn.Module):
    def __init__(self, unet, betas=(1e-4, 0.02), steps=200, drop_prob=0.1, device="cpu", predict_score=False):
        super().__init__()
        self.unet = unet.to(device)
        self.device = device
        self.steps = steps
        self.drop_prob = drop_prob
        self.predict_score = predict_score
        self.loss_mse = nn.MSELoss()
        sched = ddpm_schedules(betas[0], betas[1], steps)
        for k, v in sched.items(): self.register_buffer(k, v)

    def forward(self, x0, c_vec):
        B = x0.size(0)
        t = torch.randint(1, self.steps, (B,), device=self.device)
        noise = torch.randn_like(x0)
        x_t = self.sqrtab[t, None, None, None] * x0 + self.sqrtmab[t, None, None, None] * noise
        context_mask = torch.bernoulli(torch.full((B,), self.drop_prob, device=self.device)).long()
        t_scaled = (t / self.steps).float().unsqueeze(-1)
        pred = self.unet(x_t, c_vec, t_scaled, context_mask)
        target = - self.sqrtmab[t, None, None, None] * pred if self.predict_score else noise
        return self.loss_mse(pred if not self.predict_score else -pred, noise if not self.predict_score else target)

    @torch.no_grad()
    def sample(self, n, size, c_vec, guide_w=2.0):
        device = self.device
        x = torch.randn(n, *size, device=device)
        if c_vec.size(0) == 1: c_vec = c_vec.repeat(n, 1)
        for i in range(self.steps, 0, -1):
            t_scaled = torch.full((n,1), i/self.steps, device=device)
            x_in = x.repeat(2,1,1,1)
            c_in = torch.cat([c_vec, c_vec], dim=0)
            cmask = torch.zeros(n*2, device=device).long(); cmask[n:] = 1
            pred = self.unet(x_in, c_in, t_scaled.repeat(2,1), cmask)
            pred1, pred2 = pred[:n], pred[n:]
            pred = (1 + guide_w) * pred1 - guide_w * pred2
            eps = pred if not self.predict_score else - self.sqrtmab[i] * pred
            z = torch.randn_like(x) if i > 1 else 0.0
            x = self.oneover_sqrta[i] * (x - eps * self.mab_over_sqrtmab[i]) + self.sqrt_beta_t[i] * z
        return x

    # ------- NEW: image-to-image helpers -------
    @torch.no_grad()
    def q_sample(self, x0, t_int, noise=None):
        B = x0.size(0)
        if isinstance(t_int, int):
            t = torch.full((B,), int(t_int), device=self.device, dtype=torch.long)
        else:
            t = t_int.to(self.device).long(); assert t.numel() == B
        if noise is None:
            noise = torch.randn_like(x0)
        s = self.sqrtab[t].view(B, 1, 1, 1)
        m = self.sqrtmab[t].view(B, 1, 1, 1)
        return s * x0 + m * noise

    @torch.no_grad()
    def sample_i2i(self, x_t, t_start, c_vec, guide_w=2.0):
        device = self.device
        B = x_t.size(0)
        x = x_t.clone()
        if c_vec.size(0) == 1:
            c_vec = c_vec.repeat(B, 1)
        for i in range(int(t_start), 0, -1):
            t_scaled = torch.full((B, 1), i / self.steps, device=device)
            x_in = x.repeat(2, 1, 1, 1)
            c_in = torch.cat([c_vec, c_vec], dim=0)
            cmask = torch.zeros(B * 2, device=device, dtype=torch.long); cmask[B:] = 1
            pred = self.unet(x_in, c_in, t_scaled.repeat(2, 1), cmask)
            pred1, pred2 = pred[:B], pred[B:]
            pred = (1 + guide_w) * pred1 - guide_w * pred2
            eps = pred if not self.predict_score else - self.sqrtmab[i] * pred
            z = torch.randn_like(x) if i > 1 else 0.0
            x = self.oneover_sqrta[i] * (x - eps * self.mab_over_sqrtmab[i]) + self.sqrt_beta_t[i] * z
        return x

