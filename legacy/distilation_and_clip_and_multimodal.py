def train_enhanced_nanoCLIP(epochs=5, batch_size=256, emb_dim=128, lr=1e-3):
    """Enhanced training with validation and metrics"""

    train_loader, val_loader, vocab, train_pairs, val_pairs = build_enhanced_mnist_loaders(batch_size)
    device = "cuda" if torch.cuda.is_available() else "cpu"
    console.print(f"[green]Training on {device}[/green]")
    console.print(f"[blue]Vocabulary size: {len(vocab)}[/blue]")
    console.print(f"[blue]Train samples: {len(train_pairs)}, Val samples: {len(val_pairs)}[/blue]")

    # Models
    img_enc = EnhancedImageEncoder(emb_dim).to(device)
    txt_enc = EnhancedTextEncoder(vocab, emb_dim).to(device)
    criterion = ContrastiveLoss().to(device)

    # Optimizer with weight decay
    optimizer = torch.optim.AdamW(
        list(img_enc.parameters()) + list(txt_enc.parameters()) + list(criterion.parameters()),
        lr=lr, weight_decay=1e-4
    )

    # Learning rate scheduler
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, epochs * len(train_loader))

    best_val_loss = float('inf')
    train_losses, val_losses = [], []

    with Live(console=console, refresh_per_second=1) as live:
        for epoch in range(1, epochs + 1):
            # Training
            img_enc.train()
            txt_enc.train()
            train_loss = 0

            for i, (imgs, texts) in enumerate(train_loader):
                imgs = imgs.to(device)

                img_emb = img_enc(imgs)
                txt_emb = txt_enc(texts)

                loss, temp = criterion(img_emb, txt_emb)

                optimizer.zero_grad()
                loss.backward()
                torch.nn.utils.clip_grad_norm_(
                    list(img_enc.parameters()) + list(txt_enc.parameters()), 1.0
                )
                optimizer.step()
                scheduler.step()

                train_loss += loss.item()

                if i % 200 == 0:
                    # Create training table
                    table = Table(title=f"Epoch {epoch}/{epochs}")
                    table.add_column("Metric", style="cyan")
                    table.add_column("Value", justify="right", style="magenta")

                    table.add_row("Batch", f"{i}/{len(train_loader)}")
                    table.add_row("Train Loss", f"{loss.item():.4f}")
                    table.add_row("Temperature", f"{temp.item():.4f}")
                    table.add_row("Learning Rate", f"{scheduler.get_last_lr()[0]:.6f}")

                    # Show retrieval example
                    with torch.no_grad():
                        sims = (img_emb @ txt_emb.T)[0].softmax(dim=0)
                        top_idx = sims.argmax().item()

                    example_panel = Panel(
                        f"True: {texts[0]}\nPred: {texts[top_idx]}\nScore: {sims[top_idx]:.3f}",
                        title="Sample Retrieval"
                    )

                    live.update(Panel.fit(table, title="Training Progress"))
                    console.print(example_panel)

            # Validation
            img_enc.eval()
            txt_enc.eval()
            val_loss = 0
            all_img_embs, all_txt_embs = [], []

            with torch.no_grad():
                for imgs, texts in val_loader:
                    imgs = imgs.to(device)

                    img_emb = img_enc(imgs)
                    txt_emb = txt_enc(texts)

                    loss, _ = criterion(img_emb, txt_emb)
                    val_loss += loss.item()

                    all_img_embs.append(img_emb.cpu())
                    all_txt_embs.append(txt_emb.cpu())

            # Compute metrics
            all_img_embs = torch.cat(all_img_embs).to(device)
            all_txt_embs = torch.cat(all_txt_embs).to(device)
            metrics = compute_retrieval_metrics(all_img_embs, all_txt_embs)

            avg_train_loss = train_loss / len(train_loader)
            avg_val_loss = val_loss / len(val_loader)

            train_losses.append(avg_train_loss)
            val_losses.append(avg_val_loss)

            # Display epoch results
            results_table = Table(title=f"Epoch {epoch} Results")
            results_table.add_column("Metric", style="cyan")
            results_table.add_column("Value", justify="right", style="magenta")

            results_table.add_row("Train Loss", f"{avg_train_loss:.4f}")
            results_table.add_row("Val Loss", f"{avg_val_loss:.4f}")
            results_table.add_row("I2T R@1", f"{metrics['i2t_r@1']:.3f}")
            results_table.add_row("I2T R@5", f"{metrics['i2t_r@5']:.3f}")
            results_table.add_row("T2I R@1", f"{metrics['t2i_r@1']:.3f}")
            results_table.add_row("T2I R@5", f"{metrics['t2i_r@5']:.3f}")

            console.print(Panel.fit(results_table))

            # Save best model
            if avg_val_loss < best_val_loss:
                best_val_loss = avg_val_loss
                torch.save({
                    "img_encoder": img_enc.state_dict(),
                    "txt_encoder": txt_enc.state_dict(),
                    "vocab": vocab,
                    "config": {
                        "emb_dim": emb_dim,
                        "vocab_size": len(vocab)
                    }
                }, "nanoclip_v2_enhanced_best.pt")
                console.print("[green]✓ Saved best model[/green]")

    return img_enc, txt_enc, vocab, train_pairs, val_pairs, train_losses, val_losses

