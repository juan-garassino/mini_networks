@torch.no_grad()
def image_to_image(
    model,
    src_img,                     # Tensor (1,H,W) or (C,H,W) or (1,C,H,W)
    stoi=None,
    text: str = None,            # optional text condition
    w_img: float = 0.7,          # image embedding weight
    w_txt: float = 0.3,          # text embedding weight
    strength: float = 0.6,       # 0..1 noise amount
    guide_w: float = 2.0,
    use_ddim: bool = True,
    ddim_steps: int = 50,
    eta: float = 0.0,
):
    device = next(model.parameters()).device
    model.eval()

    # prep image
    if src_img.ndim == 2:
        src = src_img.unsqueeze(0)
    elif src_img.ndim == 3 and src_img.size(0) in (1,):
        src = src_img
    elif src_img.ndim == 4:
        src = src_img[0]
    else:
        src = src_img

    img_size = 32 if model.use_latent else 28
    to_size = T.Compose([T.Resize(img_size)])
    x0 = to_size(src.unsqueeze(0)).to(device)  # (1,1,H,W)
    if x0.min() >= 0 and x0.max() <= 1.0:
        x0 = (x0 - 0.5) / 0.5  # to [-1,1]

    # conditioning
    img_emb = model.img_enc(x0)
    cond = img_emb
    if text and stoi is not None and w_txt > 0:
        toks = [stoi.get(t, 0) for t in text.lower().split()]
        toks = toks[:model.cap_dec.max_len] + [stoi["<pad>"]] * (model.cap_dec.max_len - len(toks))
        enc_tok = torch.tensor(toks, dtype=torch.long, device=device).unsqueeze(0)
        txt_emb = model.txt_enc(enc_tok)
        cond = F.normalize(w_img * img_emb + w_txt * txt_emb, dim=-1)

    # timestep from strength
    S = int(model.diff.steps)
    strength = float(max(0.0, min(1.0, strength)))
    t_start = max(1, int(round(strength * (S - 1))))

    if model.use_latent:
        with torch.no_grad():
            mu, _ = model.vae.encode(x0)
            z0 = mu
        z_t = model.diff.q_sample(z0, t_start)
        if use_ddim:
            z_out = model.diff.sample_ddim_i2i(z_t, t_start, cond, guide_w=guide_w, eta=eta, ddim_steps=ddim_steps)
        else:
            z_out = model.diff.sample_i2i(z_t, t_start, cond, guide_w=guide_w)
        imgs = model.vae.decode(z_out)
    else:
        x_t = model.diff.q_sample(x0, t_start)
        imgs = model.diff.sample_i2i(x_t, t_start, cond, guide_w=guide_w)

    show_images(imgs, title=f'I2I (strength={strength}, guide={guide_w})' + (f' + "{text}"' if text else ""), nrow=4, value_range=(-1,1))
    return imgs

@torch.no_grad()
def text_to_text(model, prompt, stoi, itos, max_len=16, max_new_tokens=12, temperature=1.0, top_k=0, top_p=1.0):
    toks = ["<bos>"] + prompt.lower().split()
    ids = [stoi.get(t, 0) for t in toks]
    ids = ids[:max_len]
    y0 = torch.tensor(ids, dtype=torch.long, device=DEVICE)
    out_ids = model.lm.generate(
        y0, max_new_tokens=max_new_tokens, temperature=temperature, top_k=top_k, top_p=top_p
    ).tolist()
    words = []
    for tid in out_ids:
        if tid in (model.lm.bos_id, model.lm.pad_id): continue
        if tid == model.lm.eos_id: break
        words.append(itos.get(tid, ""))
    return " ".join([w for w in words if w])

