def generate_rich_captions():
    """Generate diverse captions with templates and descriptors"""

    # Base templates
    templates = [
        "a {descriptor} {item}",
        "an image of a {descriptor} {item}",
        "this is a {descriptor} {item}",
        "a {style} {item}",
        "a picture showing a {descriptor} {item}",
        "{item} with {descriptor} appearance",
        "handwritten {item}",
        "a clear {item}",
    ]

    # Descriptors for variety
    descriptors = [
        "clear", "bold", "faint", "thick", "thin", "dark", "light",
        "handwritten", "printed", "stylized", "simple", "complex"
    ]

    styles = [
        "stylish", "elegant", "casual", "formal", "modern", "classic"
    ]

    # MNIST Digits - richer captions
    mnist_captions = []
    for i in range(10):
        base_items = [f"digit {i}", f"number {i}", f"numeral {i}"]
        for item in base_items:
            for template in templates:
                if "{descriptor}" in template:
                    for desc in descriptors:
                        caption = template.format(descriptor=desc, item=item)
                        mnist_captions.append((i, caption))
                elif "{style}" in template:
                    for style in styles:
                        caption = template.format(style=style, item=item)
                        mnist_captions.append((i, caption))
                else:
                    caption = template.format(item=item)
                    mnist_captions.append((i, caption))

    # FashionMNIST - enhanced captions
    fmnist_items = [
        "t-shirt", "trouser", "pullover", "dress", "coat",
        "sandal", "shirt", "sneaker", "bag", "ankle boot"
    ]
    fmnist_captions = []
    for i, item in enumerate(fmnist_items):
        base_items = [item, f"fashion {item}", f"clothing {item}"]
        for base_item in base_items:
            for template in templates:
                if "{descriptor}" in template:
                    for desc in descriptors + styles:
                        caption = template.format(descriptor=desc, item=base_item)
                        fmnist_captions.append((i, caption))
                elif "{style}" in template:
                    for style in styles:
                        caption = template.format(style=style, item=base_item)
                        fmnist_captions.append((i, caption))
                else:
                    caption = template.format(item=base_item)
                    fmnist_captions.append((i, caption))

    # EMNIST Letters - enhanced captions
    emnist_captions = []
    for i in range(1, 27):  # 1-26 for a-z (EMNIST uses 1-26, not 0-25)
        letter = chr(i + 96)  # convert to lowercase letter
        base_items = [f"letter {letter}", f"character {letter}", f"alphabet {letter}"]
        for base_item in base_items:
            for template in templates:
                if "{descriptor}" in template:
                    for desc in descriptors:
                        caption = template.format(descriptor=desc, item=base_item)
                        emnist_captions.append((i, caption))  # Keep 1-26 indexing
                elif "{style}" in template:
                    for style in styles:
                        caption = template.format(style=style, item=base_item)
                        emnist_captions.append((i, caption))
                else:
                    caption = template.format(item=base_item)
                    emnist_captions.append((i, caption))

    return mnist_captions, fmnist_captions, emnist_captions

# ---------------------------
# Enhanced Data Loading with Augmentation
# ---------------------------
def build_enhanced_mnist_loaders(batch_size=256, val_split=0.1):
    """Build loaders with data augmentation and richer captions"""

    # Training transforms with augmentation
    train_tf = T.Compose([
        T.Resize(32),
        T.RandomRotation(10),
        T.RandomAffine(degrees=0, translate=(0.1, 0.1)),
        T.ToTensor(),
        T.Normalize((0.5,), (0.5,))  # Normalize to [-1, 1]
    ])

    # Validation transforms (no augmentation)
    val_tf = T.Compose([
        T.Resize(32),
        T.ToTensor(),
        T.Normalize((0.5,), (0.5,))
    ])

    # Generate rich captions
    mnist_captions, fmnist_captions, emnist_captions = generate_rich_captions()

    # Load datasets
    mnist = tv.datasets.MNIST("./data", train=True, download=True, transform=train_tf)
    fmnist = tv.datasets.FashionMNIST("./data", train=True, download=True, transform=train_tf)
    emnist = tv.datasets.EMNIST("./data", split="letters", train=True, download=True, transform=train_tf)

    # Create pairs with multiple captions per image
    pairs = []

    # MNIST pairs
    for img, y in mnist:
        # Select random caption for this class
        class_captions = [cap for cls, cap in mnist_captions if cls == y]
        caption = random.choice(class_captions)
        pairs.append((img, caption))

    # FashionMNIST pairs
    for img, y in fmnist:
        class_captions = [cap for cls, cap in fmnist_captions if cls == y]
        caption = random.choice(class_captions)
        pairs.append((img, caption))

    # EMNIST pairs - EMNIST uses labels 1-26 for letters a-z
    for img, y in emnist:
        class_captions = [cap for cls, cap in emnist_captions if cls == y]
        if class_captions:  # Safety check
            caption = random.choice(class_captions)
            pairs.append((img, caption))
        else:
            # Fallback caption if no match found
            if 1 <= y <= 26:
                letter = chr(y + 96)
                caption = f"letter {letter}"
                pairs.append((img, caption))

    # Shuffle and split
    random.shuffle(pairs)
    split_idx = int(len(pairs) * (1 - val_split))
    train_pairs = pairs[:split_idx]
    val_pairs = pairs[split_idx:]

    class CLIPDataset(torch.utils.data.Dataset):
        def __init__(self, pairs, transform=None):
            self.pairs = pairs
            self.transform = transform

        def __len__(self):
            return len(self.pairs)

        def __getitem__(self, idx):
            img, txt = self.pairs[idx]
            if self.transform:
                img = self.transform(img)
            return img, txt

    # Create datasets
    train_ds = CLIPDataset(train_pairs)
    val_ds = CLIPDataset(val_pairs)

    # Create loaders
    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True, num_workers=2)
    val_loader = DataLoader(val_ds, batch_size=batch_size, shuffle=False, num_workers=2)

    # Build vocabulary from all captions
    all_captions = [cap for _, cap in pairs]
    vocab = sorted(set(' '.join(all_captions).split()))

    return train_loader, val_loader, vocab, train_pairs, val_pairs

