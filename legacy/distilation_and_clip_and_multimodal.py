        ddim_steps = int(max(1, min(ddim_steps, S)))

        # choose evenly spaced timesteps in [1..S], descending
        # e.g., S=200, ddim_steps=50  ->  t = [200, 196, 192, ..., 4]
        idx = torch.linspace(0, S - 1, steps=ddim_steps, device=device).long()
        t_seq = (S - idx).long()   # values in [1..S], length=ddim_steps

        x = torch.randn(n, *size, device=device)
        if c_vec.size(0) == 1:
            c_vec = c_vec.repeat(n, 1)

