class ResidualConvBlock(nn.Module):
    def __init__(self, in_ch, out_ch, is_res=False):
        super().__init__()
        self.is_res = is_res; self.same = in_ch == out_ch
        self.c1 = nn.Sequential(nn.Conv2d(in_ch, out_ch, 3, 1, 1), nn.BatchNorm2d(out_ch), nn.GELU())
        self.c2 = nn.Sequential(nn.Conv2d(out_ch, out_ch, 3, 1, 1), nn.BatchNorm2d(out_ch), nn.GELU())
    def forward(self, x):
        if self.is_res:
            x1 = self.c1(x); x2 = self.c2(x1)
            out = (x + x2) if self.same else (x1 + x2)
            return out / 1.41421356
        else:
            return self.c2(self.c1(x))

class UnetDown(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.seq = nn.Sequential(ResidualConvBlock(in_ch, out_ch), nn.MaxPool2d(2))
    def forward(self, x): return self.seq(x)

class UnetUp(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.up = nn.ConvTranspose2d(out_ch, out_ch, 2, 2)
        self.r1 = ResidualConvBlock(in_ch, out_ch)
        self.r2 = ResidualConvBlock(out_ch, out_ch)
    def forward(self, x, skip):
        x = self.up(x)
        x = torch.cat([x, skip], dim=1)
        x = self.r1(x); x = self.r2(x)
        return x

class EmbedFC(nn.Module):
    def __init__(self, in_dim, out_dim):
        super().__init__()
        self.net = nn.Sequential(nn.Linear(in_dim, out_dim), nn.GELU(), nn.Linear(out_dim, out_dim))
    def forward(self, x): return self.net(x)

class ContextUNetVec(nn.Module):
    """U-Net conditioned on arbitrary context vectors (e.g., text embeddings)."""
    def __init__(self, in_ch=1, n_feat=128, cond_dim=128):
        super().__init__()
        self.init = ResidualConvBlock(in_ch, n_feat, is_res=True)
        self.d1 = UnetDown(n_feat, n_feat)       # 28->14
        self.d2 = UnetDown(n_feat, 2*n_feat)     # 14->7
        self.to_vec = nn.Sequential(nn.AvgPool2d(7), nn.GELU())
        # time and context embeddings
        self.temb1 = EmbedFC(1, 2*n_feat)
        self.temb2 = EmbedFC(1, 1*n_feat)
        self.cemb1 = EmbedFC(cond_dim, 2*n_feat)
        self.cemb2 = EmbedFC(cond_dim, 1*n_feat)
        # up path
        self.up0 = nn.Sequential(nn.ConvTranspose2d(2*n_feat, 2*n_feat, 7, 7), nn.GroupNorm(8, 2*n_feat), nn.ReLU())
        self.u1 = UnetUp(4*n_feat, n_feat)
        self.u2 = UnetUp(2*n_feat, n_feat)
        self.out = nn.Sequential(nn.Conv2d(2*n_feat, n_feat, 3,1,1), nn.GroupNorm(8, n_feat), nn.ReLU(),
                                 nn.Conv2d(n_feat, in_ch, 3,1,1))
    def forward(self, x, c_vec, t_scaled, context_mask):
        x0 = self.init(x); d1 = self.d1(x0); d2 = self.d2(d1)
        hid = self.to_vec(d2)
        # classifier-free: mask context
        c_masked = c_vec * (1.0 - context_mask.float().unsqueeze(-1))
        t1 = self.temb1(t_scaled).view(-1, 2*self.d1.seq[0].c1[0].out_channels, 1, 1)  # 2*n_feat
        t2 = self.temb2(t_scaled).view(-1, self.d1.seq[0].c1[0].out_channels, 1, 1)    # n_feat
        c1 = self.cemb1(c_masked).view(-1, 2*self.d1.seq[0].c1[0].out_channels, 1, 1)
        c2 = self.cemb2(c_masked).view(-1, self.d1.seq[0].c1[0].out_channels, 1, 1)
        u0 = self.up0(hid)
        u1 = self.u1(c1 * u0 + t1, d2)
        u2 = self.u2(c2 * u1 + t2, d1)
        return self.out(torch.cat([u2, x0], dim=1))

def ddpm_schedules(beta1: float, beta2: float, T: int):
    beta_t = (beta2 - beta1) * torch.arange(0, T + 1, dtype=torch.float32) / T + beta1
    sqrt_beta_t = torch.sqrt(beta_t)
    alpha_t = 1.0 - beta_t
    alphabar_t = torch.cumsum(torch.log(alpha_t), dim=0).exp()
    sqrtab = torch.sqrt(alphabar_t)
    oneover_sqrta = 1.0 / torch.sqrt(alpha_t)
    sqrtmab = torch.sqrt(1.0 - alphabar_t)
    mab_over_sqrtmab = (1.0 - alpha_t) / torch.clamp(sqrtmab, min=1e-12)
    return {"alpha_t":alpha_t,"oneover_sqrta":oneover_sqrta,"sqrt_beta_t":sqrt_beta_t,
            "alphabar_t":alphabar_t,"sqrtab":sqrtab,"sqrtmab":sqrtmab,"mab_over_sqrtmab":mab_over_sqrtmab}

class DDPMVec(nn.Module):
    def __init__(self, unet, betas=(1e-4, 0.02), steps=200, drop_prob=0.1, device="cpu"):
        super().__init__()
        self.unet = unet.to(device)
        self.device = device
        self.steps = steps
        self.drop_prob = drop_prob
        self.loss_mse = nn.MSELoss()
        sched = ddpm_schedules(betas[0], betas[1], steps)
        for k, v in sched.items(): self.register_buffer(k, v)

    def forward(self, x0, c_vec):
        B = x0.size(0)
        t = torch.randint(1, self.steps, (B,), device=self.device)
        noise = torch.randn_like(x0)
        x_t = self.sqrtab[t, None, None, None] * x0 + self.sqrtmab[t, None, None, None] * noise
        # classifier-free: random drop context
        context_mask = torch.bernoulli(torch.full((B,), self.drop_prob, device=self.device)).long()
        t_scaled = (t / self.steps).float().unsqueeze(-1)
        pred = self.unet(x_t, c_vec, t_scaled, context_mask)
        return self.loss_mse(pred, noise)

    @torch.no_grad()
    def sample(self, n, size, c_vec, guide_w=2.0):
        """Classifier-free guidance sampling. c_vec: (B,d) or (1,d)."""
        device = self.device
        x = torch.randn(n, *size, device=device)
        if c_vec.size(0) == 1:
            c_vec = c_vec.repeat(n, 1)
        for i in range(self.steps, 0, -1):
            t_scaled = torch.full((n,1), i/self.steps, device=device)
            # duplicate for cond/uncond
            x_in = x.repeat(2,1,1,1)
            c_in = torch.cat([c_vec, c_vec], dim=0)
            cmask = torch.zeros(n*2, device=device).long()
            cmask[n:] = 1  # mask second half (uncond)
            eps = self.unet(x_in, c_in, t_scaled.repeat(2,1), cmask)
            eps1, eps2 = eps[:n], eps[n:]
            eps = (1 + guide_w) * eps1 - guide_w * eps2
            z = torch.randn_like(x) if i > 1 else 0.0
            x = self.oneover_sqrta[i] * (x - eps * self.mab_over_sqrtmab[i]) + self.sqrt_beta_t[i] * z
        return x

