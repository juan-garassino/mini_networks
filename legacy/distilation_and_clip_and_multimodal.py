class ResidualConvBlock(nn.Module):
    def __init__(self, in_ch, out_ch, is_res=False):
        super().__init__()
        self.is_res = is_res; self.same = in_ch == out_ch
        self.c1 = nn.Sequential(nn.Conv2d(in_ch, out_ch, 3, 1, 1), nn.BatchNorm2d(out_ch), nn.GELU())
        self.c2 = nn.Sequential(nn.Conv2d(out_ch, out_ch, 3, 1, 1), nn.BatchNorm2d(out_ch), nn.GELU())
    def forward(self, x):
        if self.is_res:
            x1 = self.c1(x); x2 = self.c2(x1)
            out = (x + x2) if self.same else (x1 + x2)
            return out / math.sqrt(2)
        else:
            return self.c2(self.c1(x))

class UnetDown(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.seq = nn.Sequential(ResidualConvBlock(in_ch, out_ch), nn.MaxPool2d(2))
    def forward(self, x): return self.seq(x)

# --- FIXED UnetUp: explicit up_in_ch, skip_ch, out_ch ---
class UnetUp(nn.Module):
    def __init__(self, up_in_ch, skip_ch, out_ch):
        super().__init__()
        # upsample from up_in_ch → out_ch, then concat with skip (skip_ch), then reduce to out_ch
        self.up = nn.ConvTranspose2d(up_in_ch, out_ch, kernel_size=2, stride=2)
        self.res1 = ResidualConvBlock(out_ch + skip_ch, out_ch)
        self.res2 = ResidualConvBlock(out_ch, out_ch)

    def forward(self, x, skip):
        x = self.up(x)
        # safety: if spatial sizes differ by 1 due to odd dims, align
        if x.shape[-2:] != skip.shape[-2:]:
            x = F.interpolate(x, size=skip.shape[-2:], mode="nearest")
        x = torch.cat([x, skip], dim=1)
        x = self.res1(x)
        x = self.res2(x)
        return x

class EmbedFC(nn.Module):
    def __init__(self, in_dim, out_dim):
        super().__init__()
        self.net = nn.Sequential(nn.Linear(in_dim, out_dim), nn.GELU(), nn.Linear(out_dim, out_dim))
    def forward(self, x): return self.net(x)

# --- Pixel U-Net (28x28) with correct skip wiring + conditioning at 2 scales ---
class ContextUNetVecPixel(nn.Module):
    def __init__(self, in_ch=1, n_feat=128, cond_dim=128):
        super().__init__()
        self.n_feat = n_feat
        # encoder
        self.init = ResidualConvBlock(in_ch, n_feat, is_res=True)     # 28x28, n_feat
        self.d1   = UnetDown(n_feat, n_feat)                          # 28→14, n_feat
        self.d2   = UnetDown(n_feat, 2*n_feat)                        # 14→7,  2*n_feat

        # time/context embeddings (two scales)
        self.temb1 = EmbedFC(1, 2*n_feat)   # deep scale (matches d2 channels)
        self.temb2 = EmbedFC(1, n_feat)     # mid scale  (matches u1 channels)
        self.cemb1 = EmbedFC(cond_dim, 2*n_feat)
        self.cemb2 = EmbedFC(cond_dim, n_feat)

        # decoder path: d2→(up,concat d1)→(up,concat x0)
        self.u1 = UnetUp(up_in_ch=2*n_feat, skip_ch=n_feat, out_ch=n_feat)  # 7→14
        self.u2 = UnetUp(up_in_ch=n_feat,   skip_ch=n_feat, out_ch=n_feat)  # 14→28

        # head: concat with x0 again (kept same as your original head)
        self.out = nn.Sequential(
            nn.Conv2d(2*n_feat, n_feat, 3, 1, 1),
            nn.GroupNorm(8, n_feat), nn.ReLU(),
            nn.Conv2d(n_feat, in_ch, 3, 1, 1),
        )

    def forward(self, x, c_vec, t_scaled, context_mask):
        B = x.size(0)
        # encoder
        x0 = self.init(x)     # [B, n_feat, 28,28]
        d1 = self.d1(x0)      # [B, n_feat, 14,14]
        d2 = self.d2(d1)      # [B, 2*n_feat, 7,7]

        # classifier-free: mask context and make embeddings
        c_masked = c_vec * (1.0 - context_mask.float().unsqueeze(-1))
        c1 = self.cemb1(c_masked).view(B, 2*self.n_feat, 1, 1)  # deep scale
        t1 = self.temb1(t_scaled).view(B, 2*self.n_feat, 1, 1)
        c2 = self.cemb2(c_masked).view(B, self.n_feat, 1, 1)    # mid scale
        t2 = self.temb2(t_scaled).view(B, self.n_feat, 1, 1)

        # inject conditioning at matching scales
        d2 = d2 + c1 + t1                 # deep (7x7, 2*n_feat)
        u1 = self.u1(d2, d1)              # up to 14x14, concat d1 inside → [B, n_feat, 14,14]
        u1 = u1 + c2 + t2                 # mid (14x14, n_feat)
        u2 = self.u2(u1, x0)              # up to 28x28, concat x0 inside → [B, n_feat, 28,28]

        return self.out(torch.cat([u2, x0], dim=1))  # [B, in_ch, 28,28]


