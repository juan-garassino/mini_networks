def train_ddpm(
    epochs=2, dataset="fashion", batch_size=128, lr=1e-4,
    n_diffusion_steps=200, n_features=128, drop_prob=0.1,
    subset=10000, guide_weight_eval=2.0
):
    train_loader, n_classes = get_ddpm_loader(dataset, batch_size, subset)
    model = ContextUnet(in_channels=1, n_feat=n_features, n_classes=n_classes)
    ddpm = DDPM(model, betas=(1e-4, 0.02), n_diffusion_steps=n_diffusion_steps, device=DEVICE, drop_prob=drop_prob).to(DEVICE)
    opt = torch.optim.Adam(ddpm.parameters(), lr=lr)
    for ep in range(1, epochs + 1):
        ddpm.train(); loss_ema = None
        pbar = tqdm(train_loader, desc=f"[DDPM] Epoch {ep}/{epochs}")
        for x, c in pbar:
            x = x.to(DEVICE); c = c.to(DEVICE)
            opt.zero_grad(); loss = ddpm(x, c); loss.backward()
            torch.nn.utils.clip_grad_norm_(ddpm.parameters(), 1.0)
            opt.step()
            loss_ema = loss.item() if loss_ema is None else 0.95 * loss_ema + 0.05 * loss.item()
            pbar.set_postfix(loss=f"{loss_ema:.4f}")
        ddpm.eval()
        with torch.no_grad():
            samples, _ = ddpm.sample(n_samples=16, size=(1,28,28), device=DEVICE, guide_weight=guide_weight_eval, n_classes=n_classes)
        show_images(samples, title=f"{dataset.upper()} DDPM Samples (ep {ep})")
    return ddpm, n_classes

