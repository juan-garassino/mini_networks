def get_ddpm_loader(dataset="fashion", batch_size=128, subset=10000, num_workers=2):
    tfm = T.Compose([T.Resize(32), T.ToTensor(), T.Normalize((0.5,), (0.5,))])
    root = "./data"
    if dataset.lower() == "mnist":
        ds = tv.datasets.MNIST(root, train=True, download=True, transform=tfm); n = 10
    elif dataset.lower() == "fashion":
        ds = tv.datasets.FashionMNIST(root, train=True, download=True, transform=tfm); n = 10
    else: raise ValueError("dataset must be 'mnist' or 'fashion'")
    if subset is not None and subset < len(ds):
        idxs = torch.randperm(len(ds))[:subset].tolist(); ds = Subset(ds, idxs)
    loader = DataLoader(ds, batch_size=batch_size, shuffle=True, num_workers=num_workers, pin_memory=True)
    return loader, n

def train_vae(epochs=2, batch_size=128, lr=1e-3, dataset="fashion", subset=10000):
    loader, _ = get_ddpm_loader(dataset, batch_size, subset)
    vae = VAE().to(DEVICE); opt = torch.optim.Adam(vae.parameters(), lr=lr)
    best_loss = float('inf')
    for ep in range(1, epochs + 1):
        vae.train(); train_loss = 0
        pbar = tqdm(loader, desc=f"[VAE] Epoch {ep}/{epochs}")
        for x, _ in pbar:
            x = x.to(DEVICE); recon, mu, logvar = vae(x)
            recon_loss = F.mse_loss(recon, x, reduction='mean')
            kl_loss = -0.5 * (1 + logvar - mu.pow(2) - logvar.exp()).mean()
            loss = recon_loss + 0.001 * kl_loss
            opt.zero_grad(); loss.backward(); opt.step()
            train_loss += loss.item(); pbar.set_postfix(loss=f"{loss.item():.4f}")
        avg_loss = train_loss / len(loader)
        if avg_loss < best_loss:
            best_loss = avg_loss; torch.save(vae.state_dict(), "vae.pt")
            print(Fore.GREEN + "✓ Saved best VAE" + Style.RESET_ALL)
    vae.eval(); return vae

def train_ldm(epochs=2, dataset="fashion", batch_size=128, lr=1e-4, n_diffusion_steps=200,
              n_features=128, drop_prob=0.1, predict_score=False, subset=10000,
              guide_weight_eval=2.0, vae=None):
    train_loader, n_classes = get_ddpm_loader(dataset, batch_size, subset)
    model = ContextUnet(in_channels=vae.latent_channels, n_feat=n_features, n_classes=n_classes)
    ldm = LDM(model, betas=(1e-4, 0.02), n_diffusion_steps=n_diffusion_steps, device=DEVICE,
              drop_prob=drop_prob, predict_score=predict_score).to(DEVICE)
    opt = torch.optim.Adam(ldm.parameters(), lr=lr)
    for ep in range(1, epochs + 1):
        ldm.train(); loss_ema = None
        pbar = tqdm(train_loader, desc=f"[LDM] Epoch {ep}/{epochs}")
        for x, c in pbar:
            x, c = x.to(DEVICE), c.to(DEVICE)
            with torch.no_grad():
                mu, _ = vae.encode(x); z = mu
            opt.zero_grad(); loss = ldm(z, c); loss.backward()
            torch.nn.utils.clip_grad_norm_(ldm.parameters(), 1.0)
            opt.step()
            loss_ema = loss.item() if loss_ema is None else 0.95 * loss_ema + 0.05 * loss.item()
            pbar.set_postfix(loss=f"{loss_ema:.4f}")
        ldm.eval()
        with torch.no_grad():
            latents, _ = ldm.sample(n_samples=16, size=(vae.latent_channels, 8, 8), device=DEVICE,
                                    guide_weight=guide_weight_eval, n_classes=n_classes)
            samples = vae.decode(latents)
        show_images(samples, title=f"{dataset.upper()} LDM Samples (ep {ep})")
    return ldm, n_classes, vae

