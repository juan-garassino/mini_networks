class VAE(nn.Module):
    def __init__(self, latent_channels=4):
        super().__init__()
        self.latent_channels = latent_channels

        # Encoder: 32x32 -> 16x16 -> 8x8 -> 8x8
        self.encoder = nn.Sequential(
            nn.Conv2d(1, 32, 3, 1, 1), nn.BatchNorm2d(32), nn.LeakyReLU(0.2, inplace=True),  # 32x32
            nn.Conv2d(32, 64, 3, 2, 1), nn.BatchNorm2d(64), nn.LeakyReLU(0.2, inplace=True), # 16x16
            nn.Conv2d(64, 128, 3, 2, 1), nn.BatchNorm2d(128), nn.LeakyReLU(0.2, inplace=True),# 8x8
            nn.Conv2d(128, 128, 3, 1, 1), nn.BatchNorm2d(128), nn.LeakyReLU(0.2, inplace=True),# 8x8
            nn.Conv2d(128, latent_channels * 2, 1),  # -> [B, 2*C, 8, 8] -> split(mu, logvar)
        )

        # Decoder: 8x8 -> 16x16 -> 32x32 (exact doubling)
        self.decoder = nn.Sequential(
            nn.Conv2d(latent_channels, 128, 3, 1, 1), nn.BatchNorm2d(128), nn.LeakyReLU(0.2, inplace=True),  # 8x8
            nn.ConvTranspose2d(128, 64, 4, 2, 1), nn.BatchNorm2d(64), nn.LeakyReLU(0.2, inplace=True),        # 16x16
            nn.ConvTranspose2d(64, 32, 4, 2, 1),  nn.BatchNorm2d(32), nn.LeakyReLU(0.2, inplace=True),        # 32x32
            nn.Conv2d(32, 1, 3, 1, 1), nn.Tanh()  # [-1,1] output, 32x32
        )

    def encode(self, x):
        h = self.encoder(x)
        mu, logvar = h.chunk(2, dim=1)  # each [B, C, 8, 8]
        return mu, logvar

    def reparam(self, mu, logvar):
        std = (0.5 * logvar).exp()
        eps = torch.randn_like(std)
        return mu + eps * std

    def decode(self, z):
        return self.decoder(z)

    def forward(self, x):
        mu, logvar = self.encode(x)
        z = self.reparam(mu, logvar)
        recon = self.decode(z)
        return recon, mu, logvar


