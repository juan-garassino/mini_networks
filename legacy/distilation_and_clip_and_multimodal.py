# ---------------------------
def distill_student(student_type="cnn", epochs=1, alpha=0.5, beta=1.0, temperature=2.0, lam=0.99,
                    batch_size=128, teacher_ckpt="vgg16_teacher_20class.pt", out_ckpt=None):
    device = "cuda" if torch.cuda.is_available() else "cpu"
    train_loader, _ = build_loaders(batch_size)

    # Load trained teacher (20-class head)
    teacher = vgg16(weights=None)
    in_features = teacher.classifier[6].in_features
    teacher.classifier[6] = nn.Linear(in_features, 20)
    teacher.load_state_dict(torch.load(teacher_ckpt, map_location=device))
    teacher.eval()
    for p in teacher.parameters():
        p.requires_grad_(False)
    teacher = teacher.to(device)

    # Student & projector
    if student_type == "cnn":
        student = SmallCNN(num_classes=20, rep_dim=128).to(device)
        rep_dim = 128
    else:
        student = TinyViT(num_classes=20).to(device)
        rep_dim = 128
    projector = FeatureProjector(512 * 7 * 7, rep_dim).to(device)

    opt = torch.optim.AdamW(list(student.parameters()) + list(projector.parameters()), lr=1e-3, weight_decay=0.05)

    ema_mean_probs = None
    teacher_feat_ma, student_feat_ma = None, None

    with Live(console=console, refresh_per_second=2) as live:
        for ep in range(1, epochs + 1):
            student.train(); projector.train()
            for i, (x, y) in enumerate(train_loader):
                x, y = x.to(device), y.to(device)

                # ---- Teacher forward (DETACHED) ----
                with torch.no_grad():
                    t_feat = teacher.features(x)
                    t_feat = teacher.avgpool(t_feat)
                    t_feat = torch.flatten(t_feat, 1).detach()
                    batch_mean = t_feat.mean(0)
                    teacher_feat_ma = batch_mean if teacher_feat_ma is None else (lam * teacher_feat_ma + (1 - lam) * batch_mean)
                    t_feat_c = (t_feat - teacher_feat_ma).detach()
                    t_logits = teacher(x).detach()
                    t_probs = F.softmax(t_logits / temperature, dim=1).detach()

                # ---- Student forward ----
                s_logits, s_rep = student(x)
                s_batch_mean = s_rep.mean(0).detach()
                student_feat_ma = s_batch_mean if student_feat_ma is None else (lam * student_feat_ma + (1 - lam) * s_batch_mean)
                s_rep_c = s_rep - student_feat_ma
                s_probs = F.softmax(s_logits / temperature, dim=1)

                # EMA collapse prevention
                if ema_mean_probs is None:
                    ema_mean_probs = s_probs.detach().mean(0)
                else:
                    ema_mean_probs = lam * ema_mean_probs + (1 - lam) * s_probs.detach().mean(0)
                ema_loss = F.mse_loss(s_probs.mean(0), ema_mean_probs)

                # losses
                ce = F.cross_entropy(s_logits, y)
                feat_loss = F.mse_loss(F.normalize(s_rep_c, dim=1), F.normalize(projector(t_feat_c), dim=1))
                kd = F.kl_div(
                    F.log_softmax(s_logits / temperature, dim=1),
                    t_probs,
                    reduction="batchmean"
                ) * (temperature * temperature)
                loss = ce + beta * feat_loss + alpha * kd + 0.1 * ema_loss

                # step
                opt.zero_grad()
                loss.backward()
                opt.step()

                if i % 250 == 0:
                    table = Table(title=f"{student_type.upper()} Ep {ep}/{epochs} • Batch {i}")
                    table.add_column("CE", justify="right")
                    table.add_column("Feat", justify="right")
                    table.add_column("KD", justify="right")
                    table.add_column("EMA", justify="right")
                    table.add_row(f"{ce:.3f}", f"{feat_loss:.3f}", f"{kd:.3f}", f"{ema_loss:.3f}")

                    topk = 5
                    t_topv, t_topi = torch.topk(t_probs[0], topk)
                    s_topv, s_topi = torch.topk(s_probs[0].detach(), topk)
                    msg = {
                        "teacher_top5": list(zip(t_topi.cpu().tolist(), np.round(t_topv.cpu().numpy(), 3).tolist())),
                        "student_top5": list(zip(s_topi.cpu().tolist(), np.round(s_topv.cpu().numpy(), 3).tolist()))
                    }
                    live.update(Panel(table))
                    console.print(Panel(str(msg), title="Softmax (sample 0)"))

    out_ckpt = out_ckpt or f"{student_type}_student.pt"
    torch.save(student.state_dict(), out_ckpt)
    console.print(f"[green]Saved {student_type} model → {out_ckpt}[/green]")
    return out_ckpt

