def build_loaders(batch_size=128, num_workers=2):
    tf = TT.Compose([
        TT.Resize(224, interpolation=TT.InterpolationMode.BICUBIC),
        TT.CenterCrop(224),                         # force exact 224×224 to keep 14×14 patches
        TT.ToTensor(),
        TT.Lambda(lambda x: x.repeat(3,1,1)),       # grayscale→3ch
        TT.Normalize((0.485,0.456,0.406),(0.229,0.224,0.225))
    ])
    mnist = tv.datasets.MNIST("./data", train=True, download=True, transform=tf)
    fmnist = tv.datasets.FashionMNIST("./data", train=True, download=True, transform=tf)
    train_ds = ConcatDataset([mnist, fmnist])
    mnist_t = tv.datasets.MNIST("./data", train=False, download=True, transform=tf)
    fmnist_t = tv.datasets.FashionMNIST("./data", train=False, download=True, transform=tf)
    test_ds = ConcatDataset([mnist_t, fmnist_t])

    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True, num_workers=num_workers)
    test_loader  = DataLoader(test_ds, batch_size=batch_size, shuffle=False, num_workers=num_workers)
    return train_loader, test_loader

# ---------------------------
# Teacher training (VGG16 → 20 classes)
# ---------------------------
def train_teacher(epochs=2, batch_size=128, lr=1e-3, freeze_conv=True, ckpt_path="vgg16_teacher_20class.pt"):
    device = "cuda" if torch.cuda.is_available() else "cpu"
    train_loader, _ = build_loaders(batch_size)

    # Load pretrained VGG and swap final classifier to 20 classes
    teacher = vgg16(weights=VGG16_Weights.IMAGENET1K_V1)
    in_features = teacher.classifier[6].in_features
    teacher.classifier[6] = nn.Linear(in_features, 20)

    # Optionally freeze conv backbone
    if freeze_conv:
        for p in teacher.features.parameters():
            p.requires_grad_(False)

    teacher = teacher.to(device)
    opt = torch.optim.Adam(filter(lambda p: p.requires_grad, teacher.parameters()), lr=lr)
    loss_fn = nn.CrossEntropyLoss()

    for ep in range(1, epochs+1):
        teacher.train()
        tot_loss, correct, total = 0.0, 0, 0
        for x,y in train_loader:
            x,y = x.to(device), y.to(device)
            logits = teacher(x)
            loss = loss_fn(logits, y)
            opt.zero_grad(); loss.backward(); opt.step()
            tot_loss += loss.item()
            correct += (logits.argmax(1) == y).sum().item()
            total += y.size(0)
        acc = correct / max(1,total)
        console.print(f"[blue]Teacher Epoch {ep}/{epochs}[/blue] | loss={tot_loss/len(train_loader):.4f} | acc={acc:.4f}")

    torch.save(teacher.state_dict(), ckpt_path)
    console.print(f"[green]Teacher saved → {ckpt_path}[/green]")
    return ckpt_path

