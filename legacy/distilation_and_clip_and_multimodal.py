# Main (with skip logic for existing checkpoints)
# ---------------------------
def main(
    teacher_epochs=2,
    teacher_freeze_conv=True,
    distill_epochs_cnn=1,
    distill_epochs_vit=1,
    temperature=2.0,
    alpha=0.5, beta=1.0, lam=0.99,
    skip_teacher_if_exists=True,
    skip_students_if_exists=True,         # NEW
    teacher_ckpt="vgg16_teacher_20class.pt",
    cnn_ckpt="cnn_student.pt",
    vit_ckpt="vit_student.pt"
):
    console.rule("[bold yellow]Train Teacher[/bold yellow]")
    if skip_teacher_if_exists and os.path.exists(teacher_ckpt):
        console.print(f"[cyan]Found existing teacher: {teacher_ckpt}. Skipping training.[/cyan]")
    else:
        train_teacher(epochs=teacher_epochs, freeze_conv=teacher_freeze_conv, ckpt_path=teacher_ckpt)

    console.rule("[bold yellow]Distill CNN[/bold yellow]")
    if skip_students_if_exists and os.path.exists(cnn_ckpt):
        console.print(f"[cyan]Found existing CNN student: {cnn_ckpt}. Skipping distillation.[/cyan]")
    else:
        distill_student("cnn", epochs=distill_epochs_cnn, alpha=alpha, beta=beta,
                        temperature=temperature, lam=lam, teacher_ckpt=teacher_ckpt, out_ckpt=cnn_ckpt)

    console.rule("[bold yellow]Distill ViT[/bold yellow]")
    if skip_students_if_exists and os.path.exists(vit_ckpt):
        console.print(f"[cyan]Found existing ViT student: {vit_ckpt}. Skipping distillation.[/cyan]")
    else:
        distill_student("vit", epochs=distill_epochs_vit, alpha=alpha, beta=beta,
                        temperature=temperature, lam=lam, teacher_ckpt=teacher_ckpt, out_ckpt=vit_ckpt)

    console.rule("[bold yellow]Inference Compare[/bold yellow]")
    t,s,v,label = inference_compare(sample_idx=42, temperature=temperature,
                                    teacher_ckpt=teacher_ckpt,
                                    cnn_ckpt=cnn_ckpt, vit_ckpt=vit_ckpt)
    print("Ground truth label:", label)

# ---------------------------
# Run
# ---------------------------
main()

