def get_most_frequent_pixel_value(image_array):
    # Convert the array to grayscale
    image_gray = np.squeeze(image_array)

    # Count the occurrences of each pixel value
    pixel_counts = np.bincount(image_gray.flatten())

    # Determine the most frequent pixel value
    most_frequent_pixel = np.argmax(pixel_counts)

    # Output '1' if the most frequent pixel value is 1, '0' otherwise
    output = '1' if most_frequent_pixel == 1 else '0'

    return output

